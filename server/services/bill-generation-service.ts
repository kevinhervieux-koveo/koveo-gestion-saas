  /**
   * Update all future auto-generated bills when the parent bill is modified.
   * @param parentBillId
   * @param updates
   */
  async updateGeneratedBillsFromParent(
    parentBillId: string,
    updates: Partial<Bill>
  ): Promise<{
    billsUpdated: number;
  }> {

    // Find all auto-generated bills for this parent
    const generatedBills = await db
      .select()
      .from(bills)
      .where(and(eq(bills.sourceTemplateId, parentBillId), eq(bills.isAutoGenerated, true)));

    let billsUpdated = 0;

    for (const generatedBill of generatedBills) {
      const updatedFields: Partial<Bill> = {};

      // Update fields that should propagate to generated bills
      if (updates.title) {
        // Preserve the date and part information in the title
        const titleParts = generatedBill.title.split(' - ');
        if (titleParts.length >= 2) {
          updatedFields.title = `${updates.title} - ${titleParts.slice(1).join(' - ')}`;
        }
      }

      if (updates.category) {
        updatedFields.category = updates.category;
      }
      if (updates.vendor) {
        updatedFields.vendor = updates.vendor;
      }
      if (updates.notes) {
        updatedFields.notes = `Auto-generated bill - ${updates.notes}`;
      }

      if (Object.keys(updatedFields).length > 0) {
        updatedFields.updatedAt = new Date();

        await db.update(bills).set(updatedFields).where(eq(bills.id, generatedBill.id));

        billsUpdated++;
      }
    }


    return { billsUpdated };
  }

  /**
   * Delete future auto-generated bills with cascade options.
   * @param parentBillId
   * @param deleteAllFuture
   */
  async deleteGeneratedBills(
    parentBillId: string,
    deleteAllFuture: boolean = false
  ): Promise<{
    billsDeleted: number;
  }> {
    // console.log(`🗑️ Deleting generated bills for parent ${parentBillId}, deleteAllFuture: ${deleteAllFuture}`);

    let whereCondition;

    if (deleteAllFuture) {
      // Delete all future auto-generated bills
      whereCondition = and(
        eq(bills.sourceTemplateId, parentBillId),
        eq(bills.isAutoGenerated, true),
        gte(bills.startDate, new Date().toISOString().split('T')[0])
      );
    } else {
      // Delete only unpaid bills (draft/sent status)
      whereCondition = and(
        eq(bills.sourceTemplateId, parentBillId),
        eq(bills.isAutoGenerated, true),
        or(eq(bills.status, 'draft'), eq(bills.status, 'sent'))
      );
    }

    const result = await db.delete(bills).where(whereCondition);

    const billsDeleted = result.rowCount || 0;

    return { billsDeleted };
  }

  /**
   * Get statistics about generated bills for a parent bill.
   * @param parentBillId
   */
  async getGeneratedBillsStats(parentBillId: string): Promise<{
    totalGenerated: number;
    paidBills: number;
    pendingBills: number;
    futureBills: number;
    totalAmount: number;
    paidAmount: number;
  }> {
    const generatedBills = await db
      .select()
      .from(bills)
      .where(and(eq(bills.sourceTemplateId, parentBillId), eq(bills.isAutoGenerated, true)));

    const today = new Date().toISOString().split('T')[0];

    const stats = {
      totalGenerated: generatedBills.length,
      paidBills: 0,
      pendingBills: 0,
      futureBills: 0,
      totalAmount: 0,
      paidAmount: 0,
    };

    for (const bill of generatedBills) {
      const billAmount = parseFloat(bill.totalAmount);
      stats.totalAmount += billAmount;

      if (bill.status === 'paid') {
        stats.paidBills++;
        stats.paidAmount += billAmount;
      } else if (bill.startDate > today) {
        stats.futureBills++;
      } else {
        stats.pendingBills++;
      }
    }

    return stats;
  }

