  /**
   * Calculate appropriate end date for auto-generated bill.
   * @param sourceBill
   * @param startDate
   */
  private calculateEndDate(sourceBill: Bill, startDate: Date): string | null {
    if (!sourceBill.endDate) {
      return null;
    }

    // For recurring bills, calculate relative end date
    const originalStart = new Date(sourceBill.startDate);
    const originalEnd = new Date(sourceBill.endDate);
    const daysDifference = Math.floor((originalEnd.getTime() - originalStart.getTime()) / (1000 * 60 * 60 * 24));
    
    const newEndDate = new Date(startDate);
    newEndDate.setDate(newEndDate.getDate() + daysDifference);
    
    return newEndDate.toISOString().split('T')[0];
  }

