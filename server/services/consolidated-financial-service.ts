  /**
   * Get financial data for a building with smart caching
   */
  async getFinancialData(
    buildingId: string,
    startDate: string,
    endDate: string,
    forceRefresh = false
  ): Promise<FinancialPeriodData> {
    return this.executeWithErrorHandling('getFinancialData', async () => {
      const cacheKey = this.generateCacheKey(startDate, endDate);

      if (!forceRefresh) {
        const cached = await this.getCachedData(buildingId, cacheKey, startDate, endDate);
        if (cached) {
          return cached;
        }
      }

      const financialData = await this.calculateFinancialData(buildingId, startDate, endDate);
      await this.cacheFinancialData(buildingId, cacheKey, startDate, endDate, financialData);

      return financialData;
    });
  }

  private async calculateFinancialData(
    buildingId: string,
    startDate: string,
    endDate: string
  ): Promise<FinancialPeriodData> {
    const activeBills = await db
      .select()
      .from(bills)
      .where(
        and(
          eq(bills.buildingId, buildingId),
          eq(bills.paymentType, 'recurrent'),
          sql`${bills.status} IN ('sent', 'draft')`
        )
      );

    const activeResidences = await db
      .select()
      .from(residences)
      .where(
        and(
          eq(residences.buildingId, buildingId),
          eq(residences.isActive, true),
          sql`${residences.monthlyFees} > 0`
        )
      );

    const monthlyData = this.generateMonthlyDataPoints(activeBills, activeResidences, startDate, endDate);
    const summary = this.calculateSummary(monthlyData);

    return {
      buildingId,
      startDate,
      endDate,
      monthlyData,
      summary,
    };
  }

  private generateMonthlyDataPoints(
    bills: Bill[],
    residences: Residence[],
    startDateStr: string,
    endDateStr: string
  ): FinancialPeriodData['monthlyData'] {
    const monthlyData: FinancialPeriodData['monthlyData'] = [];
    const startDate = new Date(startDateStr);
    const endDate = new Date(endDateStr);

    const currentDate = new Date(startDate);
    while (currentDate <= endDate) {
      const monthKey = `${currentDate.getFullYear()}-${String(currentDate.getMonth() + 1).padStart(2, '0')}`;
      
      // Calculate income from residences
      const monthlyIncome = residences.reduce((total, residence) => {
        return total + parseFloat(residence.monthlyFees?.toString() || '0');
      }, 0);

      // Calculate expenses from bills
      const monthlyExpenses = bills.reduce((total, bill) => {
        return total + parseFloat(bill.totalAmount?.toString() || '0');
      }, 0);

      monthlyData.push({
        month: monthKey,
        income: monthlyIncome,
        expenses: monthlyExpenses,
        netCashFlow: monthlyIncome - monthlyExpenses,
        occupancyRate: 95, // Default occupancy rate
        maintenanceCosts: monthlyExpenses * 0.3, // Estimate 30% for maintenance
      });

      currentDate.setMonth(currentDate.getMonth() + 1);
    }

    return monthlyData;
  }

  private calculateSummary(monthlyData: FinancialPeriodData['monthlyData']): FinancialPeriodData['summary'] {
    const totalIncome = monthlyData.reduce((sum, month) => sum + month.income, 0);
    const totalExpenses = monthlyData.reduce((sum, month) => sum + month.expenses, 0);
    const netCashFlow = totalIncome - totalExpenses;
    const averageOccupancy = monthlyData.reduce((sum, month) => sum + month.occupancyRate, 0) / monthlyData.length;

    return {
      totalIncome,
      totalExpenses,
      netCashFlow,
      averageOccupancy,
      profitMargin: totalIncome > 0 ? (netCashFlow / totalIncome) * 100 : 0,
    };
  }

