  /**
   * Generate payment plan for selected vendor
   */
  async generatePaymentPlan(
    vendorId: string,
    totalCost: number,
    schedule: 'weekly' | 'monthly' | 'quarterly' | 'yearly' | 'custom',
    customDates?: Date[],
    startDate?: Date
  ): Promise<PaymentPlan> {
    return this.executeWithErrorHandling('generatePaymentPlan', async () => {
      this.validatePaymentPlanInputs(totalCost, schedule, customDates);

      const planStartDate = startDate || new Date();
      const paymentSchedule = this.calculatePaymentSchedule(totalCost, schedule, planStartDate, customDates);
      
      return {
        costs: paymentSchedule.amounts,
        schedule,
        customDates: schedule === 'custom' ? paymentSchedule.dates.map(d => d.toISOString().split('T')[0]) : undefined,
        startDate: planStartDate.toISOString().split('T')[0],
        totalAmount: totalCost
      };
    });
  }

  private validatePaymentPlanInputs(
    totalCost: number,
    schedule: string,
    customDates?: Date[]
  ): void {
    if (totalCost <= 0) {
      throw new Error('Total cost must be greater than 0');
    }

    if (schedule === 'custom' && (!customDates || customDates.length === 0)) {
      throw new Error('Custom schedule requires custom dates');
    }
  }

  private calculatePaymentSchedule(
    totalCost: number,
    schedule: string,
    startDate: Date,
    customDates?: Date[]
  ): { amounts: number[]; dates: Date[] } {
    if (schedule === 'custom' && customDates) {
      const amountPerPayment = totalCost / customDates.length;
      return {
        amounts: customDates.map(() => amountPerPayment),
        dates: customDates
      };
    }

    // Standard schedules
    const intervals = {
      weekly: 7,
      monthly: 30,
      quarterly: 90,
      yearly: 365
    };

    const interval = intervals[schedule as keyof typeof intervals] || 30;
    const numberOfPayments = schedule === 'yearly' ? 1 : Math.max(2, Math.ceil(365 / interval));
    const amountPerPayment = totalCost / numberOfPayments;

    const dates: Date[] = [];
    const amounts: number[] = [];

    for (let i = 0; i < numberOfPayments; i++) {
      const paymentDate = new Date(startDate);
      paymentDate.setDate(paymentDate.getDate() + (i * interval));
      dates.push(paymentDate);
      amounts.push(amountPerPayment);
    }

    return { amounts, dates };
  }

