  /**
   * Create an auto-generated bill from template and persist it to database
   */
  async createAutoGeneratedBill(
    templateBill: Bill, 
    newBillData: Partial<Bill>, 
    suggestedDate?: Date
  ): Promise<Bill> {
    return this.executeWithErrorHandling('createAutoGeneratedBill', async () => {
      if (templateBill.paymentType !== 'recurrent') {
        throw new Error('Can only create auto-generated bills from recurrent bills');
      }

      const startDate = suggestedDate || new Date();
      const uniqueBillNumber = await this.generateUniqueBillNumber(templateBill.category, startDate);
      
      const newBillData_prepared = {
        ...templateBill,
        ...newBillData,
        billNumber: uniqueBillNumber,
        paymentType: 'unique' as const,
        // NEW: Set payment structure fields for single payment
        paymentCount: '1' as const,
        recurrence: false,
        singlePaymentAmount: templateBill.totalAmount,
        // Clear multi-payment configuration
        hasInitialPayment: false,
        recurringPaymentsEqual: true,
        initialPaymentAmount: '',
        recurringPaymentAmount: '',
        customPayments: [],
        startDate: startDate.toISOString().split('T')[0],
        endDate: newBillData.endDate || templateBill.endDate,
        status: 'draft' as const,
        isAutoGenerated: false,
        sourceTemplateId: templateBill.sourceTemplateId,
        autoGeneratedLabel: null,
        createdAt: new Date(),
        updatedAt: new Date(),
      };

      // Remove the id field as it will be auto-generated on insert
      delete (newBillData_prepared as any).id;

      // Insert the bill into the database
      const insertedBills = await db.insert(bills).values(newBillData_prepared).returning();
      
      if (!insertedBills || insertedBills.length === 0) {
        throw new Error('Failed to insert auto-generated bill into database');
      }

      const insertedBill = insertedBills[0];
      // console.log(`✅ Created and persisted auto-generated bill: ${insertedBill.id} (${insertedBill.billNumber})`);

      // Generate payments for the newly created bill
      await this.generatePaymentsForBill(insertedBill.id);

      // Update source bill end date to prevent double counting
      if (templateBill.sourceTemplateId) {
        await this.updateSourceBillEndDate(templateBill.sourceTemplateId, startDate);
      }

      return insertedBill;
    });
  }

  /**
   * Generate unique bill number using timestamp + UUID approach
   */
  private async generateUniqueBillNumber(category: string, date: Date): Promise<string> {
    const year = date.getFullYear();
    const month = String(date.getMonth() + 1).padStart(2, '0');
    const day = String(date.getDate()).padStart(2, '0');
    const categoryCode = category.toUpperCase().replace(/[^A-Z]/g, '').substring(0, 4);
    
    const timestamp = Date.now();
    const timestampStr = timestamp.toString(36).toUpperCase();
    
    return `${categoryCode}-${year}${month}${day}-${timestampStr}`;
  }

  private async updateSourceBillEndDate(sourceTemplateId: string, startDate: Date): Promise<void> {
    const endDate = new Date(startDate);
    endDate.setDate(endDate.getDate() - 1);
    
    await db
      .update(bills)
      .set({
        endDate: endDate.toISOString().split('T')[0],
        updatedAt: new Date()
      })
      .where(eq(bills.id, sourceTemplateId));
  }

