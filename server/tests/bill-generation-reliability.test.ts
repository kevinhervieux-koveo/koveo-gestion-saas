// @ts-nocheck — Pre-existing type errors tracked in TYPE_CHECK_DEBT.md (task #769)
import { BillAutoGenerationService } from '../services/bill-generation-service';
import { describe, it, expect, beforeEach } from '@jest/globals';
import type { Bill } from '../../shared/schema';

describe('Bill Auto-Generation Reliability Tests', () => {
  let service: BillAutoGenerationService;
  let mockRecurrentBill: Bill;

  beforeEach(() => {
    service = new BillAutoGenerationService();
    
    // Create a mock recurrent bill for testing
    mockRecurrentBill = {
      id: 'test-bill-001',
      buildingId: 'building-001',
      billNumber: 'BILL-2024-001',
      title: 'Monthly Insurance Premium',
      description: 'Building insurance coverage',
      category: 'insurance',
      vendor: 'Insurance Corp',
      paymentType: 'recurrent',
      schedulePayment: 'monthly',
      scheduleCustom: null,
      costs: [1500.00],
      totalAmount: '1500.00',
      startDate: '2024-01-15',
      endDate: null,
      status: 'draft',
      filePath: null,
      fileName: null,
      fileSize: null,
      isAiAnalyzed: false,
      aiAnalysisData: null,
      notes: 'Monthly premium for building insurance',
      isAutoGenerated: false,
      sourceTemplateId: null,
      autoGeneratedLabel: null,
      createdBy: 'user-001',
      createdAt: new Date('2024-01-01'),
      updatedAt: new Date('2024-01-01'),
    };
  });

  describe('Custom Schedule Pattern Testing', () => {
    it('should handle quarterly custom dates correctly', () => {
      const customBill = {
        ...mockRecurrentBill,
        schedulePayment: 'custom',
        scheduleCustom: ['2024-03-15', '2024-06-15', '2024-09-15', '2024-12-15']
      };

      const startDate = new Date('2024-01-01');
      const endDate = new Date('2024-12-31');
      
      const dueDates = service.calculateNextDueDates(
        'custom', 
        startDate, 
        endDate, 
        customBill.scheduleCustom
      );

      expect(dueDates).toHaveLength(4);
      expect(dueDates[0].getMonth()).toBe(2); // March (0-indexed)
      expect(dueDates[1].getMonth()).toBe(5); // June
      expect(dueDates[2].getMonth()).toBe(8); // September  
      expect(dueDates[3].getMonth()).toBe(11); // December
    });

    it('should handle bi-weekly custom pattern', () => {
      // Create bi-weekly pattern starting from January 1st
      const biWeeklyDates = [];
      const start = new Date('2024-01-01');
      for (let i = 0; i < 26; i++) { // 26 bi-weekly periods in a year
        const date = new Date(start);
        date.setDate(start.getDate() + (i * 14));
        biWeeklyDates.push(date.toISOString().split('T')[0]);
      }

      const customBill = {
        ...mockRecurrentBill,
        schedulePayment: 'custom',
        scheduleCustom: biWeeklyDates
      };

      const dueDates = service.calculateNextDueDates(
        'custom',
        new Date('2024-01-01'),
        new Date('2024-12-31'),
        customBill.scheduleCustom
      );

      expect(dueDates.length).toBeGreaterThan(20);
      expect(dueDates.length).toBeLessThan(30);
      
      // Verify 14-day intervals
      for (let i = 1; i < Math.min(dueDates.length, 5); i++) {
        const daysDiff = (dueDates[i].getTime() - dueDates[i-1].getTime()) / (1000 * 60 * 60 * 24);
        expect(daysDiff).toBe(14);
      }
    });

    it('should handle irregular custom dates', () => {
      const irregularDates = [
        '2024-01-31', // End of January
        '2024-04-15', // Mid April
        '2024-07-04', // Holiday
        '2024-10-31', // Halloween
        '2024-12-25'  // Christmas
      ];

      const dueDates = service.calculateNextDueDates(
        'custom',
        new Date('2024-01-01'),
        new Date('2024-12-31'),
        irregularDates
      );

      expect(dueDates).toHaveLength(5);
      expect(dueDates[0].getDate()).toBe(31); // January 31st
      expect(dueDates[1].getMonth()).toBe(3); // April (0-indexed)
      expect(dueDates[2].getMonth()).toBe(6); // July
      expect(dueDates[3].getMonth()).toBe(9); // October
      expect(dueDates[4].getMonth()).toBe(11); // December
    });
  });

  describe('Date Calculation Edge Cases', () => {
    it('should handle month boundary transitions correctly', () => {
      const monthlyBill = {
        ...mockRecurrentBill,
        schedulePayment: 'monthly',
        startDate: '2024-01-31' // January 31st
      };

      const dueDates = service.calculateNextDueDates(
        'monthly',
        new Date('2024-01-31'),
        new Date('2024-06-30')
      );

      // Should handle February correctly (28 days in 2024)
      expect(dueDates[1].getDate()).toBe(28); // February 28th
      expect(dueDates[1].getMonth()).toBe(1); // February

      // March should go back to 31st
      expect(dueDates[2].getDate()).toBe(31); // March 31st
      expect(dueDates[2].getMonth()).toBe(2); // March

      // April has only 30 days
      expect(dueDates[3].getDate()).toBe(30); // April 30th
      expect(dueDates[3].getMonth()).toBe(3); // April
    });

    it('should handle leap year February 29th correctly', () => {
      // Test with 2024 (leap year)
      const yearlyBill = {
        ...mockRecurrentBill,
        schedulePayment: 'yearly',
        startDate: '2024-02-29' // Leap day
      };

      const dueDates = service.calculateNextDueDates(
        'yearly',
        new Date('2024-02-29'),
        new Date('2027-03-01')
      );

      expect(dueDates[0].getDate()).toBe(29); // 2024 Feb 29 (leap year)
      expect(dueDates[1].getDate()).toBe(28); // 2025 Feb 28 (non-leap year)
      expect(dueDates[2].getDate()).toBe(28); // 2026 Feb 28 (non-leap year)
      expect(dueDates[3].getDate()).toBe(28); // 2027 Feb 28 (non-leap year)
    });

    it('should handle quarterly end-of-month dates', () => {
      const quarterlyBill = {
        ...mockRecurrentBill,
        schedulePayment: 'quarterly',
        startDate: '2024-03-31' // March 31st
      };

      const dueDates = service.calculateNextDueDates(
        'quarterly',
        new Date('2024-03-31'),
        new Date('2024-12-31')
      );

      expect(dueDates[0].getDate()).toBe(31); // March 31st
      expect(dueDates[1].getDate()).toBe(30); // June 30th (June has 30 days)
      expect(dueDates[2].getDate()).toBe(30); // September 30th
      expect(dueDates[3].getDate()).toBe(31); // December 31st
    });

    it('should handle business day adjustments', () => {
      // Test date that falls on a weekend
      const weekendCustomDates = [
        '2024-01-06', // Saturday
        '2024-01-07', // Sunday
        '2024-01-08'  // Monday
      ];

      const dueDates = service.calculateNextDueDates(
        'custom',
        new Date('2024-01-01'),
        new Date('2024-01-31'),
        weekendCustomDates
      );

      // Saturday should be moved to Monday
      expect(dueDates[0].getDay()).not.toBe(6); // Not Saturday
      expect(dueDates[0].getDay()).not.toBe(0); // Not Sunday

      // Sunday should be moved to Monday
      expect(dueDates[1].getDay()).not.toBe(0); // Not Sunday

      // Monday should remain Monday
      expect(dueDates[2].getDay()).toBe(1); // Monday
    });
  });

  describe('Generation Validation Rules', () => {
    it('should reject non-recurrent bills', () => {
      const uniqueBill = { ...mockRecurrentBill, paymentType: 'unique' as const };
      
      expect(() => {
        service.generateForNextYear(uniqueBill);
      }).toThrow('does not meet auto-generation criteria');
    });

    it('should reject bills without schedule', () => {
      const noScheduleBill = { ...mockRecurrentBill, schedulePayment: null };
      
      expect(() => {
        service.generateForNextYear(noScheduleBill);
      }).toThrow('does not meet auto-generation criteria');
    });

    it('should reject cancelled or overdue bills', () => {
      const cancelledBill = { ...mockRecurrentBill, status: 'cancelled' as const };
      const overdueBill = { ...mockRecurrentBill, status: 'overdue' as const };
      
      expect(() => {
        service.generateForNextYear(cancelledBill);
      }).toThrow('does not meet auto-generation criteria');

      expect(() => {
        service.generateForNextYear(overdueBill);
      }).toThrow('does not meet auto-generation criteria');
    });

    it('should reject bills with invalid amounts', () => {
      const zeroAmountBill = { ...mockRecurrentBill, totalAmount: '0' };
      const negativeAmountBill = { ...mockRecurrentBill, totalAmount: '-100' };
      
      expect(() => {
        service.generateForNextYear(zeroAmountBill);
      }).toThrow('does not meet auto-generation criteria');

      expect(() => {
        service.generateForNextYear(negativeAmountBill);
      }).toThrow('does not meet auto-generation criteria');
    });

    it('should reject auto-generated bills from generating more bills', () => {
      const autoGeneratedBill = { ...mockRecurrentBill, isAutoGenerated: true };
      
      expect(() => {
        service.generateForNextYear(autoGeneratedBill);
      }).toThrow('does not meet auto-generation criteria');
    });
  });

  describe('Data Integrity', () => {
    it('should preserve all essential data in generated bills', () => {
      const generated = service.generateForNextYear(mockRecurrentBill);
      
      generated.forEach(bill => {
        expect(bill.buildingId).toBe(mockRecurrentBill.buildingId);
        expect(bill.title).toBe(mockRecurrentBill.title);
        expect(bill.description).toBe(mockRecurrentBill.description);
        expect(bill.category).toBe(mockRecurrentBill.category);
        expect(bill.vendor).toBe(mockRecurrentBill.vendor);
        expect(bill.totalAmount).toBe(mockRecurrentBill.totalAmount);
        expect(bill.costs).toEqual(mockRecurrentBill.costs);
        expect(bill.paymentType).toBe('unique');
        expect(bill.isAutoGenerated).toBe(true);
        expect(bill.sourceTemplateId).toBe(mockRecurrentBill.id);
        expect(bill.status).toBe('draft');
      });
    });

    it('should generate unique bill numbers', () => {
      const generated = service.generateForNextYear(mockRecurrentBill);
      const billNumbers = generated.map(bill => bill.billNumber);
      const uniqueBillNumbers = new Set(billNumbers);
      
      expect(uniqueBillNumbers.size).toBe(billNumbers.length);
    });

    it('should generate meaningful auto-generated labels', () => {
      const generated = service.generateForNextYear(mockRecurrentBill);
      
      generated.forEach(bill => {
        expect(bill.autoGeneratedLabel).toBeTruthy();
        expect(bill.autoGeneratedLabel).toContain('Monthly');
        expect(bill.autoGeneratedLabel).toContain('2025'); // Next year
      });
    });

    it('should handle large date ranges without performance issues', () => {
      const startTime = Date.now();
      
      // Test with 10 years of weekly bills (500+ dates)
      const dueDates = service.calculateNextDueDates(
        'weekly',
        new Date('2024-01-01'),
        new Date('2034-01-01')
      );
      
      const endTime = Date.now();
      const executionTime = endTime - startTime;
      
      expect(dueDates.length).toBeGreaterThan(500);
      expect(executionTime).toBeLessThan(1000); // Should complete in under 1 second
    });
  });

  describe('Custom Schedule Validation', () => {
    it('should handle empty custom schedule arrays', () => {
      expect(() => {
        service.calculateNextDueDates('custom', new Date(), new Date(), []);
      }).not.toThrow();
      
      const dueDates = service.calculateNextDueDates('custom', new Date(), new Date(), []);
      expect(dueDates).toHaveLength(0);
    });

    it('should handle invalid date strings in custom schedules', () => {
      const invalidDates = ['invalid-date', '2024-13-01', '2024-02-30'];
      
      expect(() => {
        service.calculateNextDueDates('custom', new Date(), new Date(), invalidDates);
      }).not.toThrow(); // Should handle gracefully
    });

    it('should sort custom dates chronologically', () => {
      const unsortedDates = ['2024-12-15', '2024-03-15', '2024-06-15', '2024-09-15'];
      
      const dueDates = service.calculateNextDueDates(
        'custom',
        new Date('2024-01-01'),
        new Date('2024-12-31'),
        unsortedDates
      );
      
      // Should be sorted chronologically
      for (let i = 1; i < dueDates.length; i++) {
        expect(dueDates[i].getTime()).toBeGreaterThan(dueDates[i-1].getTime());
      }
    });
  });
});