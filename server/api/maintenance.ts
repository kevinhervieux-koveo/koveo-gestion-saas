  app.put('/api/maintenance/history/:id', requireAuth, async (req: any, res) => {
    try {
      const user = req.user;
      if (!user) {
        return res.status(401).json({ error: 'Authentication required' });
      }
      
      if (!['admin', 'manager'].includes(user.role)) {
        return res.status(403).json({
          error: 'Insufficient permissions to update element history'
        });
      }
      
      const { id } = req.params;
      const validation = elementHistoryCreateSchema.partial().safeParse(req.body);
      if (!validation.success) {
        return res.status(400).json({
          error: 'Invalid history data',
          details: validation.error.issues
        });
      }
      
      // Fetch full existing row for diff computation and access check
      const existingRows = await db
        .select()
        .from(elementHistory)
        .innerJoin(buildingElements, eq(elementHistory.elementId, buildingElements.id))
        .where(eq(elementHistory.id, id))
        .limit(1);
      
      if (existingRows.length === 0) {
        return res.status(404).json({ error: 'History entry not found' });
      }
      
      const existing = existingRows[0].element_history;
      const buildingId = existingRows[0].building_elements.buildingId;

      const hasAccess = await checkBuildingAccess(user.id, buildingId, user.role);
      if (!hasAccess) {
        return res.status(403).json({
          error: 'No access to this history entry'
        });
      }
      
      // Prepare update data, handling date fields and missing properties properly
      const fieldUpdates: Record<string, unknown> = {};
      
      if (validation.data.eventType !== undefined) fieldUpdates.eventType = validation.data.eventType;
      if (validation.data.eventDate) fieldUpdates.eventDate = validation.data.eventDate.toISOString().split('T')[0];
      if (validation.data.description !== undefined) fieldUpdates.workDescription = validation.data.description;
      if (validation.data.cost !== undefined) fieldUpdates.cost = validation.data.cost;
      if (validation.data.vendorId !== undefined) fieldUpdates.vendorId = validation.data.vendorId;
      if (validation.data.warrantyEndDate) {
        fieldUpdates.warranty = { endDate: validation.data.warrantyEndDate.toISOString().split('T')[0] };
      }

      // Build structured before/after diff. Skip fields that haven't changed.
      // cost is stored as a decimal string in the DB; normalize both sides for comparison.
      const normalizeCost = (v: unknown): string | null =>
        v == null ? null : parseFloat(String(v)).toString();
      const diffFields: Record<string, { before: unknown; after: unknown }> = {};

      if ('eventType' in fieldUpdates && fieldUpdates.eventType !== existing.eventType) {
        diffFields.eventType = { before: existing.eventType, after: fieldUpdates.eventType };
      }
      if ('eventDate' in fieldUpdates && fieldUpdates.eventDate !== existing.eventDate) {
        diffFields.eventDate = { before: existing.eventDate, after: fieldUpdates.eventDate };
      }
      if ('workDescription' in fieldUpdates && fieldUpdates.workDescription !== existing.workDescription) {
        diffFields.workDescription = { before: existing.workDescription, after: fieldUpdates.workDescription };
      }
      if ('cost' in fieldUpdates && normalizeCost(fieldUpdates.cost) !== normalizeCost(existing.cost)) {
        diffFields.cost = { before: existing.cost, after: fieldUpdates.cost };
      }
      if ('vendorId' in fieldUpdates && fieldUpdates.vendorId !== existing.vendorId) {
        diffFields.vendorId = { before: existing.vendorId, after: fieldUpdates.vendorId };
      }
      if ('warranty' in fieldUpdates) {
        const beforeJson = JSON.stringify(existing.warranty ?? null);
        const afterJson = JSON.stringify(fieldUpdates.warranty ?? null);
        if (beforeJson !== afterJson) {
          diffFields.warranty = { before: existing.warranty, after: fieldUpdates.warranty };
        }
      }

      // No-op: return the existing row without touching the DB or audit log
      if (Object.keys(diffFields).length === 0) {
        return res.json({ success: true, data: existing });
      }

      const updateData: Record<string, unknown> = {
        ...fieldUpdates,
        updatedAt: new Date(),
        updatedBy: user.id,
      };

      // Recompute lastInspectionDate when the edit could shift MAX(eventDate)
      // over inspection-type events for this element — i.e. when the event
      // date or event type changed. Mirrors the MCP path (Task #1135).
      const inspectionRecomputeNeeded = 'eventDate' in diffFields || 'eventType' in diffFields;
      const elementId = existing.elementId;

      const [updatedHistory] = await db.transaction(async (tx) => {
        const [row] = await tx
          .update(elementHistory)
          .set(updateData)
          .where(eq(elementHistory.id, id))
          .returning();

        await tx.insert(elementHistoryAuditLog).values({
          historyId: id,
          performedBy: user.id,
          changes: diffFields,
        });

        if (inspectionRecomputeNeeded) {
          await recomputeLastInspectionDate(tx, elementId);
        }

        return [row];
      });
      
      res.json({
        success: true,
        data: updatedHistory
      });
    } catch (error: any) {
      console.error('Error updating element history:', error);
      res.status(500).json({
        error: 'Internal server error',
        message: 'Failed to update element history',
      });
    }
  });
  
