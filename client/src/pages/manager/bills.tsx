/**
 * BillCard function.
 * @param root0
 * @param root0.bill
 * @param root0.onUpdate
 * @returns Function result.
 */
function BillCard({ 
  bill, 
  onUpdate, 
  onAutoGeneratedClick
}: { 
  bill: Bill; 
  onUpdate: () => void;
  onAutoGeneratedClick?: (bill: Bill) => void;
}) {
  const { t } = useLanguage();
  const [showDetailDialog, setShowDetailDialog] = useState(false);
  const [showEditDialog, setShowEditDialog] = useState(false);
  const [isExpanded, setIsExpanded] = useState(false);

  // Rendering bill card with current state

  // Fetch payments for this bill when expanded
  const { data: paymentsData, isLoading: paymentsLoading } = useQuery({
    queryKey: ['/api/bills', bill.id, 'payments'],
    queryFn: async () => {
      // Fetching payments for bill
      const response = await fetch(`/api/bills/${bill.id}/payments`, {
        credentials: 'include',
      });
      // Processing payments API response
      if (!response.ok) {
        console.error('❌ [BILL CARD] Failed to fetch payments:', response.status);
        throw new Error('Failed to fetch payments');
      }
      const data = await response.json();
      // Payments data received successfully
      return data;
    },
    enabled: isExpanded, // Only fetch when expanded
  });

  const statusColors = {
    draft: 'bg-gray-100 text-gray-800',
    sent: 'bg-blue-100 text-blue-800',
    overdue: 'bg-red-100 text-red-800',
    paid: 'bg-green-100 text-green-800',
    cancelled: 'bg-gray-100 text-gray-800',
  };

  const handleCardClick = () => {
    // Bill card clicked - handling interaction
    
    if (bill.isAutoGenerated && onAutoGeneratedClick) {
      // Auto-generated bill clicked - opening workflow
      onAutoGeneratedClick(bill);
    } else {
      const newExpanded = !isExpanded;
      // Toggling bill card expansion
      setIsExpanded(newExpanded);
    }
  };

  const handleEditClick = (e: React.MouseEvent) => {
    // Edit button clicked - opening edit dialog
    e.stopPropagation(); // Prevent triggering card expansion
    setShowEditDialog(true);
  };

  return (
    <>
      <Card
        className={cn(
          'hover:shadow-md transition-shadow cursor-pointer',
          bill.isAutoGenerated && 
          'border-blue-200 bg-gradient-to-r from-blue-50/50 to-indigo-50/50 dark:border-blue-800 dark:from-blue-950/20 dark:to-indigo-950/20'
        )}
        onClick={handleCardClick}
      >
        <CardContent className='p-6 min-h-[180px]'>
          <div className='space-y-4 h-full flex flex-col'>
            <div className='flex items-start justify-between mb-2'>
              <div className="flex-1 min-h-[60px]">
                <div className="flex items-center gap-2 mb-2">
                  <h4 className='font-semibold text-base leading-tight'>{bill.title}</h4>
                  {bill.isAutoGenerated && (
                    <AutoGeneratedBadge 
                      variant="compact"
                      sourceTemplate={bill.autoGeneratedLabel}
                    />
                  )}
                </div>
                <p className='text-xs text-gray-500 mb-1'>#{bill.billNumber}</p>
                {bill.isAutoGenerated && bill.autoGeneratedLabel && (
                  <p className='text-xs text-blue-600 dark:text-blue-400 mt-1'>
                    📋 From: {bill.autoGeneratedLabel}
                  </p>
                )}
              </div>
              <div className="flex items-center gap-2">
                <Button
                  variant="outline"
                  size="sm"
                  onClick={handleEditClick}
                  className="h-8 w-8 p-0"
                  data-testid={`button-edit-bill-${bill.id}`}
                >
                  <Edit2 className="h-4 w-4" />
                </Button>
                <Badge className={statusColors[bill.status as keyof typeof statusColors]}>
                  {bill.status}
                </Badge>
                {!bill.isAutoGenerated && (
                  <div className="ml-1">
                    {isExpanded ? (
                      <ChevronUp className="h-4 w-4 text-gray-400" />
                    ) : (
                      <ChevronDown className="h-4 w-4 text-gray-400" />
                    )}
                  </div>
                )}
              </div>
            </div>

            {bill.description && (
              <div className='mb-3'>
                <p className='text-sm text-gray-600 line-clamp-2 leading-relaxed'>{bill.description}</p>
              </div>
            )}

            <div className='flex items-center justify-between text-sm py-2 border-y border-gray-100'>
              <span className='font-semibold text-lg'>${Number(bill.totalAmount).toLocaleString()}</span>
              <span className='text-gray-500 font-medium'>{bill.paymentType}</span>
            </div>

            {bill.vendor && (
              <div className='mb-2'>
                <p className='text-sm text-gray-600'>Vendor: <span className='font-medium'>{bill.vendor}</span></p>
              </div>
            )}

            <div className='flex items-center gap-2 pt-1 flex-wrap'>
              {bill.filePath && (
                <Badge variant='outline' className='text-xs'>
                  <FileText className='w-3 h-3 mr-1' />
                  Document
                </Badge>
              )}
              {bill.isAiAnalyzed && (
                <Badge variant='outline' className='text-xs'>
                  AI Analyzed
                </Badge>
              )}
              {bill.isAutoGenerated && (
                <AutoGeneratedBadge label={bill.autoGeneratedLabel} />
              )}
            </div>

            {/* Payment Details Section - shown when expanded */}
            {isExpanded && !bill.isAutoGenerated && (
              <div className="border-t pt-3 mt-3">
                <div className="flex items-center gap-2 mb-3">
                  <DollarSign className="h-4 w-4 text-blue-600" />
                  <h5 className="font-medium text-sm">Payment Schedule</h5>
                </div>
                
                {paymentsLoading ? (
                  <div className="text-sm text-gray-500">Loading payments...</div>
                ) : paymentsData?.payments?.length > 0 ? (
                  <PaymentScheduleDisplay 
                    payments={paymentsData.payments} 
                    bill={bill}
                  />
                ) : (
                  <div className="text-sm text-gray-500">No payments found</div>
                )}
              </div>
            )}
          </div>
        </CardContent>
      </Card>

      {/* Bill Detail Dialog */}
      <Dialog open={showDetailDialog} onOpenChange={setShowDetailDialog}>
        <DialogContent className='max-w-2xl max-h-[95vh] overflow-y-auto' aria-describedby="bill-details-description">
          <DialogHeader>
            <DialogTitle>Bill Details</DialogTitle>
          </DialogHeader>
          <BillDetail
            bill={bill}
            onSuccess={() => {
              setShowDetailDialog(false);
              onUpdate();
            }}
            onCancel={() => setShowDetailDialog(false)}
            onEditBill={() => {
              setShowDetailDialog(false);
              setShowEditDialog(true);
            }}
          />
        </DialogContent>
      </Dialog>

      {/* Bill Edit Dialog */}
      <Dialog open={showEditDialog} onOpenChange={setShowEditDialog}>
        <DialogContent className='max-w-4xl max-h-[95vh] overflow-y-auto' aria-describedby="edit-bill-description">
          <DialogHeader>
            <DialogTitle>{t('bills.editBill')}</DialogTitle>
          </DialogHeader>
          <ModularBillForm
            bill={bill}
            onSuccess={() => {
              onUpdate();
              setTimeout(() => setShowEditDialog(false), 100);
            }}
            onCancel={() => setShowEditDialog(false)}
            buildingId={bill.buildingId}
          />
        </DialogContent>
      </Dialog>

    </>
  );
}

// Payment Schedule Display Component
export function PaymentScheduleDisplay({ 
  payments, 
  bill 
}: { 
  payments: any[]; 
  bill: Bill; 
}) {
  const { t } = useLanguage();
  const queryClient = useQueryClient();


  const updatePaymentMutation = useMutation({
    mutationFn: async ({ paymentId, status, paidDate }: { paymentId: string; status: string; paidDate?: string }) => {
      const response = await fetch(`/api/bills/${bill.id}/payments/${paymentId}`, {
        method: 'PATCH',
        headers: {
          'Content-Type': 'application/json',
        },
        credentials: 'include',
        body: JSON.stringify({ status, paidDate }),
      });

      if (!response.ok) {
        throw new Error('Failed to update payment status');
      }

      return response.json();
    },
    onSuccess: () => {
      // Only invalidate the specific payment data to avoid collapsing the bill
      // Do NOT call onPaymentUpdate as it would invalidate the entire bills list
      queryClient.invalidateQueries({ queryKey: ['/api/bills', bill.id, 'payments'] });
    },
  });

  const getPaymentStatusColor = (status: string, isOverdue: boolean = false) => {
    switch (status) {
      case 'paid':
        return 'bg-green-100 text-green-800 dark:bg-green-900 dark:text-green-200';
      case 'overdue':
        return 'bg-red-100 text-red-800 dark:bg-red-900 dark:text-red-200';
      case 'pending':
        return isOverdue 
          ? 'bg-red-100 text-red-800 dark:bg-red-900 dark:text-red-200' 
          : 'bg-yellow-100 text-yellow-800 dark:bg-yellow-900 dark:text-yellow-200';
      case 'cancelled':
        return 'bg-gray-100 text-gray-800 dark:bg-gray-800 dark:text-gray-200';
      default:
        return 'bg-gray-100 text-gray-800 dark:bg-gray-800 dark:text-gray-200';
    }
  };

  const isPaymentOverdue = (payment: any) => {
    if (payment.status === 'paid' || payment.status === 'cancelled') return false;
    const today = new Date().toISOString().split('T')[0];
    return payment.scheduledDate < today;
  };

  const handlePaymentStatusChange = (paymentId: string, newStatus: string) => {
    
    const paidDate = newStatus === 'paid' ? new Date().toISOString() : undefined;
    updatePaymentMutation.mutate({ paymentId, status: newStatus, paidDate });
  };

  if (bill.paymentType === 'unique') {
    // Single payment line for unique bills
    const payment = payments[0];
    if (!payment) return <div className="text-sm text-gray-500">No payment information</div>;

    const overdue = isPaymentOverdue(payment);
    
    return (
      <div className="bg-white dark:bg-gray-900 border rounded-lg overflow-hidden">
        <div className="grid grid-cols-4 gap-4 p-3 bg-gray-50 dark:bg-gray-800 border-b text-xs font-medium text-gray-700 dark:text-gray-300">
          <div>Payment Date</div>
          <div>Amount</div>
          <div>Status</div>
          <div>Actions</div>
        </div>
        <div className={cn(
          "grid grid-cols-4 gap-4 p-3 items-center",
          overdue && "bg-red-50 dark:bg-red-950/20"
        )}>
          <div className="text-sm">
            <div>{payment.scheduledDate}</div>
            {payment.paidDate && (
              <div className="text-xs text-green-600 dark:text-green-400">Paid: {payment.paidDate}</div>
            )}
          </div>
          <div className="text-sm font-medium">
            ${Number(payment.amount).toLocaleString()}
          </div>
          <div>
            <Badge className={getPaymentStatusColor(payment.status, overdue)}>
              {overdue && payment.status === 'pending' ? 'overdue' : payment.status}
            </Badge>
          </div>
          <div>
            {payment.status !== 'paid' && (
              <Button
                variant="outline"
                size="sm"
                onClick={() => handlePaymentStatusChange(payment.id, 'paid')}
                disabled={updatePaymentMutation.isPending}
                className="h-7 text-xs"
                data-testid={`button-mark-paid-${payment.id}`}
              >
                Mark Paid
              </Button>
            )}
          </div>
        </div>
      </div>
    );
  } else {
    // Table with multiple payment rows for recurrent/auto-generated bills
    return (
      <div className="bg-white dark:bg-gray-900 border rounded-lg overflow-hidden">
        <div className="grid grid-cols-4 gap-4 p-3 bg-gray-50 dark:bg-gray-800 border-b text-xs font-medium text-gray-700 dark:text-gray-300">
          <div>Payment Date</div>
          <div>Amount</div>
          <div>Status</div>
          <div>Actions</div>
        </div>
        <div className="divide-y divide-gray-200 dark:divide-gray-700">
          {payments.map((payment, index) => {
            const overdue = isPaymentOverdue(payment);
            return (
              <div 
                key={payment.id} 
                className={cn(
                  "grid grid-cols-4 gap-4 p-3 items-center",
                  overdue && "bg-red-50 dark:bg-red-950/20"
                )}
              >
                <div className="text-sm">
                  <div className="flex items-center gap-2">
                    <span className="text-xs text-gray-500">#{payment.paymentNumber}</span>
                    <span>{payment.scheduledDate}</span>
                  </div>
                  {payment.paidDate && (
                    <div className="text-xs text-green-600 dark:text-green-400 mt-1">
                      Paid: {payment.paidDate}
                    </div>
                  )}
                </div>
                <div className="text-sm font-medium">
                  ${Number(payment.amount).toLocaleString()}
                </div>
                <div>
                  <Badge className={getPaymentStatusColor(payment.status, overdue)}>
                    {overdue && payment.status === 'pending' ? 'overdue' : payment.status}
                  </Badge>
                </div>
                <div>
                  {payment.status !== 'paid' && (
                    <Button
                      variant="outline"
                      size="sm"
                      onClick={() => handlePaymentStatusChange(payment.id, 'paid')}
                      disabled={updatePaymentMutation.isPending}
                      className="h-7 text-xs"
                      data-testid={`button-mark-paid-${payment.id}`}
                    >
                      Mark Paid
                    </Button>
                  )}
                </div>
              </div>
            );
          })}
        </div>
      </div>
    );
  }
}

// Bill detail component
function BillDetail({
  bill,
  onSuccess,
  onCancel,
  onEditBill,
}: {
  bill: Bill;
  onSuccess: () => void;
  onCancel: () => void;
  onEditBill: () => void;
}) {
  const queryClient = useQueryClient();

  // Fetch fresh bill data to ensure we have updated document information
  const { data: freshBill, error: freshBillError, isLoading: freshBillLoading } = useQuery({
    queryKey: ['/api/bills', bill.id],
    queryFn: async () => {
      const response = await fetch(`/api/bills/${bill.id}`, {
        credentials: 'include',
      });
      if (!response.ok) {
        throw new Error('Failed to fetch bill details');
      }
      return response.json();
    },
  });

  // Fetch documents attached to this bill
  const { data: billDocuments = [], isLoading: documentsLoading } = useQuery({
    queryKey: ['/api/documents', 'bill', bill.id],
    queryFn: async () => {
      const response = await fetch(`/api/documents?attachedToType=bill&attachedToId=${bill.id}`, {
        credentials: 'include',
      });
      if (!response.ok) {
        throw new Error('Failed to fetch bill documents');
      }
      const data = await response.json();
      return data.documents || [];
    },
  });

  // Use fresh bill data if available, fallback to props bill data
  const currentBill = freshBill || bill;
  
  
  const updateBillMutation = useMutation({
    mutationFn: async (updates: Partial<Bill>) => {
      const response = await fetch(`/api/bills/${bill.id}`, {
        method: 'PATCH',
        headers: {
          'Content-Type': 'application/json',
        },
        credentials: 'include',
        body: JSON.stringify(updates),
      });

      if (!response.ok) {
        throw new Error('Failed to update bill');
      }

      return response.json();
    },
    onSuccess: () => {
      queryClient.invalidateQueries({ queryKey: ['/api/bills'] });
      onSuccess();
    },
  });



  return (
    <div className='space-y-6'>
      {/* Bill Information */}
      <div className='grid grid-cols-2 gap-4'>
        <div>
          <Label className='text-sm font-medium'>Bill Number</Label>
          <p className='text-sm text-gray-600'>{currentBill.billNumber}</p>
        </div>
        <div>
          <Label className='text-sm font-medium'>Status</Label>
          <p className='text-sm text-gray-600 capitalize'>{currentBill.status}</p>
        </div>
        <div>
          <Label className='text-sm font-medium'>Category</Label>
          <p className='text-sm text-gray-600 capitalize'>{currentBill.category}</p>
        </div>
        <div>
          <Label className='text-sm font-medium'>Payment Type</Label>
          <p className='text-sm text-gray-600 capitalize'>{currentBill.paymentType}</p>
        </div>
        <div>
          <Label className='text-sm font-medium'>Total Amount</Label>
          <p className='text-sm text-gray-600'>${Number(currentBill.totalAmount).toLocaleString()}</p>
        </div>
        <div>
          <Label className='text-sm font-medium'>Start Date</Label>
          <p className='text-sm text-gray-600'>{currentBill.startDate}</p>
        </div>
      </div>

      {/* Title and Description */}
      <div>
        <Label className='text-sm font-medium'>Title</Label>
        <p className='text-sm text-gray-600'>{currentBill.title}</p>
      </div>

      {currentBill.description && (
        <div>
          <Label className='text-sm font-medium'>Description</Label>
          <p className='text-sm text-gray-600'>{currentBill.description}</p>
        </div>
      )}

      {currentBill.vendor && (
        <div>
          <Label className='text-sm font-medium'>Vendor</Label>
          <p className='text-sm text-gray-600'>{currentBill.vendor}</p>
        </div>
      )}

      {currentBill.notes && (
        <div>
          <Label className='text-sm font-medium'>Notes</Label>
          <p className='text-sm text-gray-600'>{currentBill.notes}</p>
        </div>
      )}


      {/* Costs Breakdown */}
      {currentBill.costs && currentBill.costs.length > 1 && (
        <div>
          <Label className='text-sm font-medium'>Payment Breakdown</Label>
          <div className='space-y-1 mt-1'>
            {currentBill.costs.map((cost, index) => (
              <div key={index} className='flex justify-between text-sm'>
                <span>Payment {index + 1}:</span>
                <span>${Number(cost).toLocaleString()}</span>
              </div>
            ))}
          </div>
        </div>
      )}

      {/* Uploaded Documents Section */}
      {(currentBill.filePath || billDocuments.length > 0) && (
        <div className='border-t pt-4'>
          <Label className='text-sm font-medium'>Uploaded Documents</Label>
          <div className='mt-2 space-y-2'>
            {/* Direct bill upload */}
            {currentBill.filePath && (
              <div className='flex items-center justify-between p-3 bg-gray-50 rounded-lg'>
                <div className='flex items-center gap-2'>
                  <FileText className='w-4 h-4 text-blue-600' />
                  <span className='text-sm'>{sanitizeFileName(currentBill.fileName)}</span>
                  {currentBill.isAiAnalyzed && (
                    <Badge variant='outline' className='text-xs'>
                      AI Analyzed
                    </Badge>
                  )}
                </div>
                <div className='flex items-center gap-2'>
                  <Button
                    variant='outline'
                    size='sm'
                    onClick={() => {
                      // View the document (open in new tab)
                      window.open(`/api/bills/${currentBill.id}/download-document`, '_blank');
                    }}
                    className='flex items-center gap-1'
                    data-testid={`button-view-document-${currentBill.id}`}
                  >
                    <FileText className='w-3 h-3' />
                    View
                  </Button>
                  <Button
                    variant='outline'
                    size='sm'
                    onClick={() => {
                      // Download the document
                      const link = document.createElement('a');
                      link.href = `/api/bills/${currentBill.id}/download-document`;
                      link.download = currentBill.fileName || 'bill-document';
                      document.body.appendChild(link);
                      link.click();
                      document.body.removeChild(link);
                    }}
                    className='flex items-center gap-1'
                    data-testid={`button-download-document-${currentBill.id}`}
                  >
                    <FileText className='w-3 h-3' />
                    Download
                  </Button>
                </div>
              </div>
            )}
            
            {/* Attached documents from documents table */}
            {billDocuments.map((doc: any) => (
              <div key={doc.id} className='flex items-center justify-between p-3 bg-blue-50 rounded-lg'>
                <div className='flex items-center gap-2'>
                  <FileText className='w-4 h-4 text-blue-600' />
                  <span className='text-sm'>{doc.name}</span>
                  <Badge variant='outline' className='text-xs'>
                    {doc.documentType || 'Document'}
                  </Badge>
                </div>
                <div className='flex items-center gap-2'>
                  <Button
                    variant='outline'
                    size='sm'
                    onClick={async () => {
                      try {
                        // Fixed: Direct window.open to API endpoint to respect Content-Disposition: inline
                        const documentUrl = `/api/documents/${doc.id}/file`;
                        const newWindow = window.open(documentUrl, '_blank');
                        
                        if (!newWindow) {
                          // Fallback: if popup blocked, show error message
                          console.error('Popup blocked - please allow popups for this site to view documents');
                          // Could add toast notification here if desired
                        }
                        
                      } catch (error) {
                        console.error('View failed:', error);
                      }
                    }}
                    className='flex items-center gap-1'
                    data-testid={`button-view-document-${doc.id}`}
                  >
                    <FileText className='w-3 h-3' />
                    View
                  </Button>
                  <Button
                    variant='outline'
                    size='sm'
                    onClick={async () => {
                      try {
                        // Use fetch with credentials to ensure authentication
                        const response = await fetch(`/api/documents/${doc.id}/file?download=true`, {
                          method: 'GET',
                          credentials: 'include', // Include authentication cookies
                        });

                        if (!response.ok) {
                          throw new Error(`Download failed: ${response.status} ${response.statusText}`);
                        }

                        // Convert response to blob and create download
                        const blob = await response.blob();
                        const url = window.URL.createObjectURL(blob);
                        
                        const link = document.createElement('a');
                        link.href = url;
                        link.download = doc.fileName || doc.name || 'document';
                        document.body.appendChild(link);
                        link.click();
                        
                        // Clean up
                        document.body.removeChild(link);
                        window.URL.revokeObjectURL(url);
                        
                      } catch (error) {
                        console.error('Download failed:', error);
                      }
                    }}
                    className='flex items-center gap-1'
                    data-testid={`button-download-document-${doc.id}`}
                  >
                    <FileText className='w-3 h-3' />
                    Download
                  </Button>
                </div>
              </div>
            ))}
          </div>
        </div>
      )}

      {/* Action Buttons */}
      <div className='flex justify-between items-center pt-4 border-t'>
        <Button
          onClick={onEditBill}
          className='flex items-center gap-1'
          data-testid={`button-edit-bill-${bill.id}`}
        >
          Edit Bill
        </Button>
        <Button variant='outline' onClick={onCancel} data-testid={`button-close-bill-modal-${bill.id}`}>
          Close
        </Button>
      </div>
    </div>
  );
}

