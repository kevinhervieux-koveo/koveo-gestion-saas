// Category labels using translation keys
const getCategoryLabel = (category: string, t: (key: string) => string) => {
  const categoryTranslationKeys: Record<string, string> = {
    administration: 'administration',
    construction: 'construction',
    consulting: 'consulting',
    equipment_rental: 'equipmentRental',
    insurance: 'insurance',
    legal_services: 'legalServices',
    maintenance: 'maintenance',
    professional_services: 'professionalServices',
    repairs: 'repairs',
    supplies: 'supplies',
    taxes: 'taxes',
    utilities: 'utilities',
    other: 'other'
  };
  return t(categoryTranslationKeys[category] || category);
};

/**
 * Filters for bills (excluding buildingId which comes from hierarchical selection)
 */
interface BillFilters {
  category: string;
  year: string;
  months: string[];
  paymentType: string;
  status: string;
  search: string;
}

/**
 * Props received from withHierarchicalSelection HOC
 */
interface BillsProps {
  organizationId?: string;
  buildingId?: string;
  buildingName?: string;
}

/**
 * Bills management component
 */
function BillsPage({ buildingId, organizationId }: BillsProps) {
  const { t } = useLanguage();
  const { toast } = useToast();
  // Bills page initializing with building and organization context
  const [, navigate] = useLocation();

  // Component initialization logging
  useEffect(() => {
    console.log('🔍 [BILLS] Component mounted', { buildingId, organizationId });
  }, []);

  // Log context changes
  useEffect(() => {
    console.log('🔍 [BILLS] Context changed:', { buildingId, organizationId });
  }, [buildingId, organizationId]);

  const handleBackToBuilding = () => {
    console.log('🔍 [BILLS] Navigating back to building selection');
    // Navigating back to building selection
    // Navigate back to building selection, removing buildingId from URL
    if (organizationId) {
      navigate(`/manager/bills?organization=${organizationId}`);
    } else {
      navigate('/manager/bills');
    }
  };

  const [filters, setFilters] = useState<BillFilters>({
    category: '',
    year: new Date().getFullYear().toString(),
    months: [],
    paymentType: '',
    status: '',
    search: '',
  });

  // Log filter changes
  useEffect(() => {
    console.log('🔍 [BILLS] Filters updated:', filters);
  }, [filters]);
  const [showCreateDialog, setShowCreateDialog] = useState(false);
  const [showPrePopulatedForm, setShowPrePopulatedForm] = useState(false);
  const [selectedAutoGeneratedBill, setSelectedAutoGeneratedBill] = useState<Bill | null>(null);
  const [showAutoGeneratedWorkflow, setShowAutoGeneratedWorkflow] = useState(false);
  const queryClient = useQueryClient();

  // Handle auto-generated bill click - NEW WORKFLOW
  const handleAutoGeneratedBillClick = (bill: Bill) => {
    if (bill.isAutoGenerated) {
      // Use the new auto-generated workflow
      setSelectedAutoGeneratedBill(bill);
      setShowAutoGeneratedWorkflow(true);
    } else {
      // Fallback to old behavior for legacy bills
      setSelectedAutoGeneratedBill(bill);
      setShowPrePopulatedForm(true);
    }
  };

  // Handle pre-populated form close
  const handlePrePopulatedFormClose = () => {
    setShowPrePopulatedForm(false);
    setSelectedAutoGeneratedBill(null);
  };


  // Fetch year range for building
  const { data: yearRange } = useQuery({
    queryKey: ['/api/bills/year-range', buildingId],
    queryFn: async () => {
      if (!buildingId) return null;
      const response = await fetch(`/api/bills/year-range?buildingId=${buildingId}`, {
        credentials: 'include'
      });
      if (!response.ok) throw new Error('Failed to fetch year range');
      return response.json();
    },
    enabled: !!buildingId
  });

  // Fetch bills based on filters
  const { data: bills = [], isLoading } = useQuery<Bill[]>({
    queryKey: ['/api/bills', buildingId, filters],
    queryFn: async () => {
      console.log('🔍 [BILLS] Fetching bills with params:', { buildingId, filters });
      const params = new URLSearchParams(); /**
       * If function.
       * @param filters.buildingId - Filters.buildingId parameter.
       */ /**
       * If function.
       * @param filters.buildingId - Filters.buildingId parameter.
       */

      if (buildingId) {
        params.set('buildingId', buildingId);
      } /**
       * If function.
       * @param filters.category && filters.category !== 'all' - filters.category && filters.category !== 'all' parameter.
       */ /**
       * If function.
       * @param filters.category && filters.category !== 'all' - filters.category && filters.category !== 'all' parameter.
       */

      if (filters.category && filters.category !== 'all') {
        params.set('category', filters.category);
      } /**
       * If function.
       * @param filters.year - Filters.year parameter.
       */ /**
       * If function.
       * @param filters.year - Filters.year parameter.
       */

      if (filters.year) {
        params.set('year', filters.year);
      } /**
       * If function.
       * @param filters.months.length > 0 - filters.months.length > 0 parameter.
       */ /**
       * If function.
       * @param filters.months.length > 0 - filters.months.length > 0 parameter.
       */

      if (filters.months.length > 0) {
        params.set('months', filters.months.join(','));
      }

      if (filters.paymentType && filters.paymentType !== 'all') {
        params.set('paymentType', filters.paymentType);
      }

      if (filters.status && filters.status !== 'all') {
        params.set('status', filters.status);
      }

      if (filters.search && filters.search.trim()) {
        params.set('search', filters.search.trim());
      }

      const url = `/api/bills${params.toString() ? '?' + params.toString() : ''}`;
      // Fetching bills with applied filters
      
      const response = await fetch(url, { credentials: 'include' }); 
      // Bills API response received

      if (!response.ok) {
        console.error('❌ [BILLS PAGE] Failed to fetch bills:', response.status, response.statusText);
        throw new Error(`Failed to fetch bills: ${response.statusText}`);
      }

      const data = await response.json();
      // Bills data received successfully
      return data;
    },
    enabled: !!buildingId, // Only fetch when buildingId is available
  });


  // Debug logging for state changes
  useEffect(() => {
    // Bills filters changed - refreshing data
  }, [filters]);

  useEffect(() => {
    // Building ID changed - updating context
  }, [buildingId]);

  useEffect(() => {
    if (bills) {
      // Bills data updated with current filter state
    }
  }, [bills]);

  useEffect(() => {
    if (isLoading) {
      // Loading bills data
    }
  }, [isLoading]);

  // Group bills by category
  const billsByCategory = bills.reduce((acc: Record<string, Bill[]>, bill: Bill) => {
    const category = bill.category || 'other'; /**
     * If function.
     * @param !acc[category] - !acc[category] parameter.
     */ /**
     * If function.
     * @param !acc[category] - !acc[category] parameter.
     */

    if (!acc[category]) {
      acc[category] = [];
    }
    acc[category].push(bill);
    return acc;
  }, {});

  const handleFilterChange = (key: keyof BillFilters, value: string | string[]) => {
    setFilters((prev) => ({ ...prev, [key]: value }));
  };

  const clearAllFilters = () => {
    setFilters({
      category: '',
      year: new Date().getFullYear().toString(),
      months: [],
      paymentType: '',
      status: '',
      search: '',
    });
  };

  const hasActiveFilters = () => {
    return (
      filters.category !== '' ||
      filters.year !== new Date().getFullYear().toString() ||
      filters.months.length > 0 ||
      filters.paymentType !== '' ||
      filters.status !== ''
    );
  };

  const handleMonthToggle = (monthValue: string) => {
    setFilters((prev) => ({
      ...prev,
      months: prev.months.includes(monthValue)
        ? prev.months.filter((m) => m !== monthValue)
        : [...prev.months, monthValue],
    }));
  };

  const handleAllMonthsToggle = () => {
    const allMonthValues = MONTHS.map((m) => m.value);
    setFilters((prev) => ({
      ...prev,
      months: prev.months.length === allMonthValues.length ? [] : allMonthValues,
    }));
  };

  const getMonthsDisplayText = () => {
    /**
     * If function.
     * @param filters.months.length === 0 - filters.months.length === 0 parameter.
     */ /**
     * If function.
     * @param filters.months.length === 0 - filters.months.length === 0 parameter.
     */

    if (filters.months.length === 0) {
      return 'All months';
    } /**
     * If function.
     * @param filters.months.length === MONTHS.length - filters.months.length === MONTHS.length parameter.
     */ /**
     * If function.
     * @param filters.months.length === MONTHS.length - filters.months.length === MONTHS.length parameter.
     */

    if (filters.months.length === MONTHS.length) {
      return 'All months';
    } /**
     * If function.
     * @param filters.months.length === 1 - filters.months.length === 1 parameter.
     */ /**
     * If function.
     * @param filters.months.length === 1 - filters.months.length === 1 parameter.
     */

    if (filters.months.length === 1) {
      const month = MONTHS.find((m) => m.value === filters.months[0]);
      return month?.label || 'All months';
    }
    return `${filters.months.length} months`;
  };

  // Get current year for year calculation
  const currentYear = new Date().getFullYear();

  // Generate year options based on actual bill data
  const getYearOptions = () => {
    if (!yearRange) {
      // Fallback to current year if no year range data
      return [currentYear];
    }

    // Show dynamic range based on actual bills with some padding
    if (!yearRange.hasBills) {
      // No bills exist, show current year ±2 years
      const startYear = currentYear - 2;
      const endYear = currentYear + 2;
      const totalYears = endYear - startYear + 1;
      return Array.from({ length: totalYears }, (_, i) => startYear + i);
    }
    
    // Use actual bill range with padding
    const minYear = yearRange.minYear ?? currentYear;
    const maxYear = yearRange.maxYear ?? currentYear;
    const startYear = Math.min(minYear, currentYear - 1);
    const endYear = Math.max(maxYear, currentYear + 1);
    const totalYears = endYear - startYear + 1;
    return Array.from({ length: totalYears }, (_, i) => startYear + i);
  };

  // Only auto-adjust initial year filter when year range loads for the first time
  // and only if the current year has no bills
  useEffect(() => {
    if (yearRange && yearRange.hasBills && filters.year === currentYear.toString()) {
      // Check if current year has bills
      const minYear = yearRange.minYear ?? currentYear;
      const maxYear = yearRange.maxYear ?? currentYear;
      const hasCurrentYearBills = minYear <= currentYear && currentYear <= maxYear;
      
      // Only auto-adjust if current year has no bills at all
      if (!hasCurrentYearBills) {
        // Use maxYear if it exists, otherwise fallback to current year
        const mostRecentYear = yearRange.maxYear ?? currentYear;
        // Auto-adjusting year filter to show relevant bills
        setFilters(prev => ({
          ...prev,
          year: mostRecentYear.toString()
        }));
      }
    }
  }, [yearRange]); // Remove filters.year and currentYear from dependencies to prevent constant re-adjustments


  return (
    <div className='flex-1 flex flex-col overflow-hidden'>
      <Header title={t('billsManagement')} subtitle={t('billsSubtitle')} />
      
      {/* Back to Building Navigation */}
      {buildingId && (
        <div className="p-4 border-b border-gray-200">
          <Button
            variant="outline"
            onClick={handleBackToBuilding}
            className="flex items-center gap-2"
            data-testid="button-back-to-building"
          >
            <ArrowLeft className="w-4 h-4" />
            {t('building')}
          </Button>
        </div>
      )}

      <div className='flex-1 overflow-auto p-6'>
        <div className='max-w-7xl mx-auto space-y-6'>
          {/* Collapsible Filters Section */}
          <CollapsibleFilters
            title={t('filters')}
            defaultExpanded={false}
            filters={[
              {
                id: 'search',
                label: 'Search',
                type: 'custom',
                customComponent: (
                  <Input
                    type='text'
                    placeholder='Search bills...'
                    value={filters.search}
                    onChange={(e) => handleFilterChange('search', e.target.value)}
                    data-testid='input-search-bills'
                  />
                ),
                value: filters.search,
                onChange: (value) => handleFilterChange('search', value as string),
              },
              {
                id: 'category',
                label: t('category'),
                type: 'custom',
                customComponent: (
                  <Select
                    value={filters.category}
                    onValueChange={(value) => handleFilterChange('category', value)}
                  >
                    <SelectTrigger>
                      <SelectValue placeholder={t('allCategories')} />
                    </SelectTrigger>
                    <SelectContent>
                      <SelectItem value='all'>{t('allCategories')}</SelectItem>
                      {BILL_CATEGORIES.map((category) => (
                        <SelectItem key={category} value={category}>
                          {getCategoryLabel(category, t)}
                        </SelectItem>
                      ))}
                    </SelectContent>
                  </Select>
                ),
                value: filters.category,
                onChange: (value) => handleFilterChange('category', value as string),
              },
              {
                id: 'year',
                label: t('year'),
                type: 'custom',
                customComponent: (
                  <Select
                    value={filters.year}
                    onValueChange={(value) => handleFilterChange('year', value)}
                  >
                    <SelectTrigger>
                      <SelectValue />
                    </SelectTrigger>
                    <SelectContent className='max-h-[300px] overflow-y-auto'>
                      {getYearOptions().map((year) => (
                        <SelectItem key={year} value={year.toString()}>
                          {year}
                          {year === currentYear && (
                            <span className='ml-2 text-xs text-blue-500'>({t('current')})</span>
                          )}
                        </SelectItem>
                      ))}
                    </SelectContent>
                  </Select>
                ),
                value: filters.year,
                onChange: (value) => handleFilterChange('year', value as string),
              },
              {
                id: 'months',
                label: t('months'),
                type: 'custom',
                customComponent: (
                  <Popover>
                    <PopoverTrigger asChild>
                      <Button
                        variant='outline'
                        className={cn(
                          'w-full justify-between',
                          filters.months.length === 0 && 'text-muted-foreground'
                        )}
                      >
                        {getMonthsDisplayText()}
                        <ChevronDown className='ml-2 h-4 w-4 shrink-0 opacity-50' />
                      </Button>
                    </PopoverTrigger>
                    <PopoverContent className='w-64 p-0' align='start'>
                      <div className='border-b p-3'>
                        <div className='flex items-center space-x-2'>
                          <Checkbox
                            id='all-months'
                            checked={filters.months.length === MONTHS.length}
                            onCheckedChange={handleAllMonthsToggle}
                          />
                          <Label
                            htmlFor='all-months'
                            className='text-sm font-medium leading-none peer-disabled:cursor-not-allowed peer-disabled:opacity-70'
                          >
                            All Months
                          </Label>
                        </div>
                      </div>
                      <div className='grid grid-cols-2 gap-2 p-3'>
                        {MONTHS.map((month) => (
                          <div key={month.value} className='flex items-center space-x-2'>
                            <Checkbox
                              id={`month-${month.value}`}
                              checked={filters.months.includes(month.value)}
                              onCheckedChange={() => handleMonthToggle(month.value)}
                            />
                            <Label
                              htmlFor={`month-${month.value}`}
                              className='text-sm leading-none peer-disabled:cursor-not-allowed peer-disabled:opacity-70'
                            >
                              {month.label}
                            </Label>
                          </div>
                        ))}
                      </div>
                    </PopoverContent>
                  </Popover>
                ),
                value: filters.months,
                onChange: (value) => handleFilterChange('months', value as string[]),
              },
              {
                id: 'paymentType',
                label: t('paymentType'),
                type: 'custom',
                customComponent: (
                  <Select
                    value={filters.paymentType}
                    onValueChange={(value) => handleFilterChange('paymentType', value)}
                  >
                    <SelectTrigger>
                      <SelectValue placeholder={t('allPaymentTypes')} />
                    </SelectTrigger>
                    <SelectContent>
                      <SelectItem value='all'>{t('allPaymentTypes')}</SelectItem>
                      <SelectItem value='unique'>{t('unique')}</SelectItem>
                      <SelectItem value='recurrent'>{t('recurrent')}</SelectItem>
                      <SelectItem value='auto-generated'>Auto-generated</SelectItem>
                    </SelectContent>
                  </Select>
                ),
                value: filters.paymentType,
                onChange: (value) => handleFilterChange('paymentType', value as string),
              },
              {
                id: 'status',
                label: t('status'),
                type: 'custom',
                customComponent: (
                  <Select
                    value={filters.status}
                    onValueChange={(value) => handleFilterChange('status', value)}
                  >
                    <SelectTrigger>
                      <SelectValue placeholder={t('allStatuses')} />
                    </SelectTrigger>
                    <SelectContent>
                      <SelectItem value='all'>{t('allStatuses')}</SelectItem>
                      <SelectItem value='draft'>{t('draft')}</SelectItem>
                      <SelectItem value='sent'>Sent</SelectItem>
                      <SelectItem value='overdue'>{t('overdue')}</SelectItem>
                      <SelectItem value='paid'>{t('paid')}</SelectItem>
                      <SelectItem value='cancelled'>{t('cancelled')}</SelectItem>
                    </SelectContent>
                  </Select>
                ),
                value: filters.status,
                onChange: (value) => handleFilterChange('status', value as string),
              },
            ]}
            activeFilters={[
              ...(filters.category && filters.category !== 'all' ? [{
                id: 'category',
                label: t('category'),
                displayValue: getCategoryLabel(filters.category, t)
              }] : []),
              {
                id: 'year',
                label: t('year'),
                displayValue: filters.year + (filters.year === new Date().getFullYear().toString() ? ` (${t('current')})` : '')
              },
              ...(filters.months.length > 0 ? [{
                id: 'months',
                label: t('months'),
                displayValue: `${filters.months.length} selected`
              }] : []),
              ...(filters.paymentType && filters.paymentType !== 'all' ? [{
                id: 'paymentType',
                label: t('paymentType'),
                displayValue: filters.paymentType
              }] : []),
              ...(filters.status && filters.status !== 'all' ? [{
                id: 'status',
                label: t('status'),
                displayValue: filters.status
              }] : []),
              ...(filters.search ? [{
                id: 'search',
                label: 'Search',
                displayValue: `"${filters.search}"`
              }] : []),
            ]}
            onReset={clearAllFilters}
            resetLabel={t('clearFilters')}
          />
          
          {/* Create Bill Section */}
          <div className='flex justify-end'>
            <Dialog open={showCreateDialog} onOpenChange={setShowCreateDialog}>
              <DialogTrigger asChild>
                <Button disabled={!buildingId}>
                  {t('createBill')}
                </Button>
              </DialogTrigger>
              <DialogContent className='max-w-4xl max-h-[90vh] overflow-y-auto'>
                <DialogHeader>
                  <DialogTitle>{t('createNewBill')}</DialogTitle>
                </DialogHeader>
                <ModularBillForm
                  buildingId={buildingId}
                  onCancel={() => setShowCreateDialog(false)}
                  onSuccess={() => {
                    setShowCreateDialog(false);
                    queryClient.invalidateQueries({ queryKey: ['/api/bills'] });
                  }}
                />
              </DialogContent>
            </Dialog>
          </div>

          {/* Bills Display */}
          {isLoading ? (
            <Card>
              <CardContent className='p-8 text-center'>
                <div className='animate-spin w-8 h-8 border-4 border-primary border-t-transparent rounded-full mx-auto mb-4'></div>
                <p className='text-gray-500'>{t('loadingBills')}</p>
              </CardContent>
            </Card>
          ) : Object.keys(billsByCategory).length === 0 ? (
            <Card>
              <CardContent className='p-8 text-center'>
                <h3 className='text-lg font-semibold text-gray-600 mb-2'>{t('noBillsFound')}</h3>
                <p className='text-gray-500 mb-4'>
                  No bills found for the selected filters. Create your first bill to get started.
                </p>
                <Button onClick={() => setShowCreateDialog(true)}>
                  Create First Bill
                </Button>
              </CardContent>
            </Card>
          ) : (
            <div className='space-y-6'>
              {BILL_CATEGORIES.filter(
                (category) =>
                  (!filters.category || filters.category === category) &&
                  billsByCategory[category]?.length > 0
              ).map((category) => (
                <BillCategorySection
                  key={category}
                  category={category}
                  bills={billsByCategory[category] || []}
                  onBillUpdate={() => queryClient.invalidateQueries({ queryKey: ['/api/bills'] })}
                  onAutoGeneratedClick={handleAutoGeneratedBillClick}
                  t={t}
                />
              ))}
            </div>
          )}
        </div>
      </div>

      {/* Create Bill Dialog */}
      <Dialog open={showCreateDialog} onOpenChange={setShowCreateDialog}>
        <DialogContent className='max-w-4xl max-h-[95vh] overflow-y-auto' aria-describedby="create-bill-description">
          <DialogHeader>
            <DialogTitle>{t('createNewBill')}</DialogTitle>
          </DialogHeader>
          <ModularBillForm
            onSuccess={() => {
              queryClient.invalidateQueries({ queryKey: ['/api/bills'] });
              setTimeout(() => setShowCreateDialog(false), 100);
            }}
            onCancel={() => setShowCreateDialog(false)}
            buildingId={buildingId}
          />
        </DialogContent>
      </Dialog>

      {/* Pre-populated Bill Form from Auto-generated Template */}
      <PrePopulatedBillForm
        bill={selectedAutoGeneratedBill}
        isOpen={showPrePopulatedForm}
        onClose={handlePrePopulatedFormClose}
        buildingId={buildingId || ''}
      />

      {/* Auto-Generated Bill Workflow */}
      <AutoGeneratedBillWorkflow
        autoGeneratedBill={selectedAutoGeneratedBill}
        isOpen={showAutoGeneratedWorkflow}
        onClose={() => {
          setShowAutoGeneratedWorkflow(false);
          setSelectedAutoGeneratedBill(null);
        }}
        onSuccess={(billId) => {
          // Bill created successfully - refreshing bill list
          queryClient.invalidateQueries({ queryKey: ['/api/bills'] });
          setShowAutoGeneratedWorkflow(false);
          setSelectedAutoGeneratedBill(null);
        }}
      />
    </div>
  );
}

