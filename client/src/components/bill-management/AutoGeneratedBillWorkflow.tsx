import { useState, useEffect, useCallback } from 'react';
import { useForm } from 'react-hook-form';
import { zodResolver } from '@hookform/resolvers/zod';
import { z } from 'zod';
import { useMutation, useQueryClient } from '@tanstack/react-query';
import { apiRequest } from '@/lib/queryClient';
import { useLanguage } from '@/hooks/use-language';
import { useToast } from '@/hooks/use-toast';
import type { Bill, Document } from '@shared/schema';
import type { UploadContext } from '@shared/config/upload-config';

import {
  Dialog,
  DialogContent,
  DialogHeader,
  DialogTitle,
} from '@/components/ui/dialog';
import {
  Form,
  FormControl,
  FormField,
  FormItem,
  FormLabel,
  FormMessage,
  FormDescription,
} from '@/components/ui/form';
import { Input } from '@/components/ui/input';
import { Textarea } from '@/components/ui/textarea';
import { Button } from '@/components/ui/button';
import {
  Select,
  SelectContent,
  SelectItem,
  SelectTrigger,
  SelectValue,
} from '@/components/ui/select';
import { Card, CardContent, CardHeader, CardTitle } from '@/components/ui/card';
import { Badge } from '@/components/ui/badge';
import { Tabs, TabsContent, TabsList, TabsTrigger } from '@/components/ui/tabs';
import { Progress } from '@/components/ui/progress';
import { Separator } from '@/components/ui/separator';
import { 
  Calendar, Sparkles, Upload, FileText, Lightbulb, X, File, Image, 
  Trash2, Eye, Download, Plus, Paperclip, CheckCircle, AlertCircle
} from 'lucide-react';
import { SharedUploader } from '@/components/document-management';
import { GeminiBillExtractor } from './GeminiBillExtractor';
import { AttachedFileSection } from '@/components/common/AttachedFileSection';

interface AutoGeneratedBillWorkflowProps {
  autoGeneratedBill: Bill | null;
  isOpen: boolean;
  onClose: () => void;
  onSuccess?: (billId: string) => void;
}

const newBillSchema = z.object({
  title: z.string().min(1, 'Title is required').max(200, 'Title must be less than 200 characters'),
  description: z.string().max(1000, 'Description must be less than 1000 characters').optional(),
  category: z.enum([
    'insurance',
    'maintenance',
    'salary',
    'utilities',
    'cleaning',
    'security',
    'landscaping',
    'professional_services',
    'administration',
    'repairs',
    'supplies',
    'taxes',
    'technology',
    'reserves',
    'other',
  ]),
  vendor: z.string().max(150, 'Vendor name must be less than 150 characters').optional(),
  totalAmount: z.string().min(1, 'Amount is required').refine((val) => {
    const num = parseFloat(val);
    return !isNaN(num) && num > 0 && num <= 999999.99;
  }, 'Amount must be between $0.01 and $999,999.99'),
  startDate: z.string().min(1, 'Start date is required').refine((val) => {
    return !isNaN(Date.parse(val));
  }, 'Start date must be a valid date'),
  endDate: z.string().optional().refine((val) => {
    if (!val) return true;
    return !isNaN(Date.parse(val));
  }, 'End date must be a valid date'),
  notes: z.string().max(2000, 'Notes must be less than 2000 characters').optional(),
});

type NewBillFormData = z.infer<typeof newBillSchema>;

export default function AutoGeneratedBillWorkflow({
  autoGeneratedBill,
  isOpen,
  onClose,
  onSuccess,
}: AutoGeneratedBillWorkflowProps) {
  const { t } = useLanguage();
  const { toast } = useToast();
  const queryClient = useQueryClient();
  
  // AI Analysis state
  const [uploadedFile, setUploadedFile] = useState<File | null>(null);
  const [attachedDocument, setAttachedDocument] = useState<Document | null>(null);
  const [aiExtractionLoading, setAiExtractionLoading] = useState(false);
  const [aiExtractionData, setAiExtractionData] = useState<any>(null);
  const [activeTab, setActiveTab] = useState<'manual' | 'ai'>('manual');
  
  // Document Management state
  const [attachedFiles, setAttachedFiles] = useState<Array<{
    id: string;
    file: File;
    preview?: string;
    uploadProgress?: number;
    aiAnalyzed?: boolean;
    category?: string;
  }>>([]);
  const [uploadProgress, setUploadProgress] = useState<{ [key: string]: number }>({});
  const [showDocumentSection, setShowDocumentSection] = useState(false);

  // Helper function to generate intelligent suggestions
  const generateSuggestions = (bill: Bill | null) => {
    if (!bill) return {};
    
    // Generate next period date suggestions
    const nextPeriodDate = new Date();
    if (bill.schedulePayment === 'monthly') {
      nextPeriodDate.setMonth(nextPeriodDate.getMonth() + 1);
    } else if (bill.schedulePayment === 'quarterly') {
      nextPeriodDate.setMonth(nextPeriodDate.getMonth() + 3);
    } else if (bill.schedulePayment === 'yearly') {
      nextPeriodDate.setFullYear(nextPeriodDate.getFullYear() + 1);
    } else {
      nextPeriodDate.setMonth(nextPeriodDate.getMonth() + 1); // Default to monthly
    }
    
    return {
      title: bill.title || '',
      description: bill.description || '',
      category: bill.category || 'other',
      vendor: bill.vendor || '',
      totalAmount: bill.totalAmount?.toString() || '',
      startDate: nextPeriodDate.toISOString().split('T')[0],
      endDate: bill.endDate || '',
      notes: bill.notes || '',
    };
  };

  // Form setup with intelligent suggestions from auto-generated bill
  const form = useForm<NewBillFormData>({
    resolver: zodResolver(newBillSchema),
    defaultValues: generateSuggestions(autoGeneratedBill)
  });
  
  // Update form when autoGeneratedBill changes
  useEffect(() => {
    if (autoGeneratedBill) {
      const suggestions = generateSuggestions(autoGeneratedBill);
      Object.entries(suggestions).forEach(([key, value]) => {
        if (value !== undefined && value !== null) {
          form.setValue(key as keyof NewBillFormData, value);
        }
      });
    }
  }, [autoGeneratedBill, form]);

  // Handle AI extraction results
  const handleAiExtractionComplete = (data: {
    success: boolean;
    formData?: any;
    confidence?: number;
    error?: string;
    rawData?: any;
    isLoading?: boolean;
  }) => {
    if (data.isLoading) {
      setAiExtractionLoading(true);
      return;
    }
    
    setAiExtractionLoading(false);
    
    if (data.success && data.formData) {
      setAiExtractionData(data);
      
      // Auto-populate form with AI-extracted data
      Object.entries(data.formData).forEach(([key, value]) => {
        if (value !== undefined && value !== null && value !== '') {
          if (key === 'totalAmount' && typeof value === 'number') {
            form.setValue(key as keyof NewBillFormData, value.toString());
          } else if (value && typeof value === 'string') {
            form.setValue(key as keyof NewBillFormData, value);
          }
        }
      });
      
      toast({
        title: 'AI Extraction Complete',
        description: `Extracted bill data with ${Math.round((data.confidence || 0) * 100)}% confidence`,
      });
      
      // Switch to manual tab to show extracted data
      setActiveTab('manual');
    } else if (data.error) {
      toast({
        title: 'AI Extraction Failed',
        description: data.error,
        variant: 'destructive',
      });
    }
  };
  
  // Handle file upload
  const handleDocumentChange = (file: File | null, text: string | null) => {
    setUploadedFile(file);
    
    if (file) {
      // Add file to attachments when uploaded for AI analysis
      addFileToAttachments(file, true);
    }
    
    if (text) {
      // Handle text input - could be enhanced with text analysis
      form.setValue('description', text);
    }
  };
  
  // Add file to attachments list
  const addFileToAttachments = useCallback((file: File, aiAnalyzed = false) => {
    const fileId = `${Date.now()}-${Math.random().toString(36).substr(2, 9)}`;
    const isImage = file.type.startsWith('image/');
    
    const newAttachment = {
      id: fileId,
      file,
      preview: isImage ? URL.createObjectURL(file) : undefined,
      uploadProgress: 0,
      aiAnalyzed,
      category: getCategoryFromFileName(file.name)
    };
    
    setAttachedFiles(prev => [...prev, newAttachment]);
    setShowDocumentSection(true);
    
    // Simulate upload progress
    let progress = 0;
    const interval = setInterval(() => {
      progress += Math.random() * 30;
      if (progress >= 100) {
        progress = 100;
        clearInterval(interval);
      }
      setUploadProgress(prev => ({ ...prev, [fileId]: progress }));
    }, 200);
  }, []);
  
  // Remove file from attachments
  const removeFileFromAttachments = useCallback((fileId: string) => {
    setAttachedFiles(prev => {
      const file = prev.find(f => f.id === fileId);
      if (file?.preview) {
        URL.revokeObjectURL(file.preview);
      }
      return prev.filter(f => f.id !== fileId);
    });
    setUploadProgress(prev => {
      const { [fileId]: removed, ...rest } = prev;
      return rest;
    });
  }, []);
  
  // Get category from file name
  const getCategoryFromFileName = (fileName: string): string => {
    const lower = fileName.toLowerCase();
    if (lower.includes('invoice') || lower.includes('bill')) return 'invoice';
    if (lower.includes('receipt')) return 'receipt';
    if (lower.includes('contract')) return 'contract';
    if (lower.includes('quote') || lower.includes('estimate')) return 'quote';
    return 'document';
  };
  
  // Handle manual file upload
  const handleManualFileUpload = useCallback((e: React.ChangeEvent<HTMLInputElement>) => {
    const files = e.target.files;
    if (files) {
      Array.from(files).forEach(file => {
        addFileToAttachments(file, false);
      });
    }
    // Reset input
    e.target.value = '';
  }, [addFileToAttachments]);

  // Create bill from auto-generated template mutation
  const createBillMutation = useMutation({
    mutationFn: async (formData: NewBillFormData) => {
      // Create FormData to handle both bill data and file uploads
      const formDataToSend = new FormData();
      
      // Add bill data
      formDataToSend.append('templateId', autoGeneratedBill?.id || '');
      formDataToSend.append('suggestedDate', formData.startDate);
      
      const newBillData = {
        title: formData.title,
        description: formData.description,
        category: formData.category,
        vendor: formData.vendor,
        totalAmount: parseFloat(formData.totalAmount),
        startDate: formData.startDate,
        endDate: formData.endDate || null,
        notes: formData.notes,
        status: 'draft' as const,
        paymentType: 'unique' as const,
        isAiAnalyzed: !!aiExtractionData,
        aiAnalysisData: aiExtractionData || null,
      };
      
      formDataToSend.append('newBillData', JSON.stringify(newBillData));
      
      // Add attached files
      attachedFiles.forEach((attachment, index) => {
        formDataToSend.append(`files`, attachment.file);
        formDataToSend.append(`fileMetadata_${index}`, JSON.stringify({
          category: attachment.category,
          aiAnalyzed: attachment.aiAnalyzed
        }));
      });
      
      // Send request with FormData (no JSON headers needed)
      const response = await fetch('/api/bills/from-auto-generated', {
        method: 'POST',
        body: formDataToSend,
        credentials: 'include'
      });
      
      if (!response.ok) {
        const errorData = await response.json().catch(() => ({}));
        throw new Error(errorData.message || `HTTP error! status: ${response.status}`);
      }
      
      return await response.json();
    },
    onSuccess: (responseData: any) => {
      queryClient.invalidateQueries({ queryKey: ['/api/bills'] });
      queryClient.invalidateQueries({ queryKey: ['/api/bills/auto-generated'] });
      
      toast({
        title: 'Success',
        description: 'Bill created successfully from auto-generated template',
      });
      
      onSuccess?.(responseData.bill?.id);
      handleClose();
    },
    onError: (error: any) => {
      toast({
        title: 'Error',
        description: error.message || 'Failed to create bill from template',
        variant: 'destructive',
      });
    }
  });
  
  // Handle dialog close with cleanup
  const handleClose = () => {
    form.reset();
    setUploadedFile(null);
    setAttachedDocument(null);
    setAiExtractionData(null);
    setAiExtractionLoading(false);
    setActiveTab('manual');
    
    // Clean up attached files and their preview URLs
    attachedFiles.forEach(file => {
      if (file.preview) {
        URL.revokeObjectURL(file.preview);
      }
    });
    setAttachedFiles([]);
    setUploadProgress({});
    setShowDocumentSection(false);
    
    onClose();
  };


  const onSubmit = (data: NewBillFormData) => {
    createBillMutation.mutate(data);
  };

  if (!autoGeneratedBill) return null;

  return (
    <Dialog open={isOpen} onOpenChange={onClose}>
      <DialogContent className="max-w-2xl max-h-[90vh] overflow-y-auto">
        <DialogHeader>
          <DialogTitle className="flex items-center gap-2">
            <Sparkles className="h-5 w-5 text-blue-500" />
            Create Bill from Auto-Generated Template
          </DialogTitle>
        </DialogHeader>

        <div className="space-y-6">
          {/* AI Analysis Workflow */}
          <Tabs value={activeTab} onValueChange={(value: 'manual' | 'ai') => setActiveTab(value)} className="w-full">
            <TabsList className="grid w-full grid-cols-2">
              <TabsTrigger value="manual" className="flex items-center gap-2">
                <Lightbulb className="h-4 w-4" />
                Manual Entry
                {aiExtractionData && (
                  <Badge variant="secondary" className="ml-2">AI Enhanced</Badge>
                )}
              </TabsTrigger>
              <TabsTrigger value="ai" className="flex items-center gap-2">
                <Sparkles className="h-4 w-4" />
                AI Analysis
              </TabsTrigger>
            </TabsList>
            
            <TabsContent value="ai" className="space-y-4">
              <Card>
                <CardHeader>
                  <CardTitle className="flex items-center gap-2">
                    <Upload className="h-5 w-5" />
                    Upload Bill Document for AI Analysis
                  </CardTitle>
                </CardHeader>
                <CardContent>
                  <SharedUploader
                    onDocumentChange={handleDocumentChange}
                    formType="bills"
                    uploadContext={{
                      type: 'bills',
                      contextId: autoGeneratedBill?.buildingId || 'unknown',
                      subContext: 'auto-generated'
                    } as UploadContext}
                    aiAnalysisEnabled={true}
                    onAiAnalysisComplete={handleAiExtractionComplete}
                    showAiToggle={false}
                    allowedFileTypes={[
                      'application/pdf',
                      'image/jpeg',
                      'image/jpg', 
                      'image/png'
                    ]}
                    maxFileSize={25}
                    className="border-dashed border-2 border-gray-300 dark:border-gray-600"
                  />
                  
                  {aiExtractionLoading && (
                    <div className="mt-4 p-4 bg-blue-50 dark:bg-blue-950 border border-blue-200 dark:border-blue-800 rounded-lg">
                      <div className="flex items-center gap-2">
                        <Sparkles className="h-4 w-4 animate-spin text-blue-500" />
                        <span className="text-sm text-blue-800 dark:text-blue-200">
                          AI is analyzing your document...
                        </span>
                      </div>
                    </div>
                  )}
                  
                  {aiExtractionData && (
                    <div className="mt-4 p-4 bg-green-50 dark:bg-green-950 border border-green-200 dark:border-green-800 rounded-lg">
                      <div className="flex items-center gap-2 mb-2">
                        <Sparkles className="h-4 w-4 text-green-500" />
                        <span className="text-sm font-medium text-green-800 dark:text-green-200">
                          AI Analysis Complete
                        </span>
                        <Badge variant="outline" className="ml-2">
                          {Math.round((aiExtractionData.confidence || 0) * 100)}% confidence
                        </Badge>
                      </div>
                      <p className="text-sm text-green-700 dark:text-green-300">
                        Form fields have been automatically populated. Switch to Manual Entry to review and modify.
                      </p>
                    </div>
                  )}
                  
                  {uploadedFile && (
                    <div className="mt-4 p-3 border border-gray-200 dark:border-gray-700 rounded-lg bg-gray-50 dark:bg-gray-800">
                      <div className="flex items-center justify-between">
                        <div className="flex items-center gap-2">
                          <FileText className="h-4 w-4 text-gray-500" />
                          <div>
                            <p className="text-sm font-medium text-gray-900 dark:text-gray-100">
                              {uploadedFile.name}
                            </p>
                            <p className="text-xs text-gray-500 dark:text-gray-400">
                              {(uploadedFile.size / 1024 / 1024).toFixed(2)} MB
                            </p>
                          </div>
                        </div>
                        <Button
                          variant="ghost"
                          size="sm"
                          onClick={() => {
                            setUploadedFile(null);
                            setAiExtractionData(null);
                          }}
                          data-testid="button-remove-file"
                        >
                          <X className="h-4 w-4" />
                        </Button>
                      </div>
                      {aiExtractionData && (
                        <div className="mt-2 p-2 bg-green-100 dark:bg-green-900 rounded text-xs">
                          <p className="text-green-700 dark:text-green-300">
                            AI extracted: {Object.keys(aiExtractionData.formData || {}).length} fields
                          </p>
                        </div>
                      )}
                    </div>
                  )}
                </CardContent>
              </Card>
            </TabsContent>
            
            <TabsContent value="manual">
              {/* Intelligent Suggestions Header */}
              {autoGeneratedBill && (
                <Card className="bg-blue-50 dark:bg-blue-950 border-blue-200 dark:border-blue-800">
                  <CardContent className="pt-4">
                    <div className="flex items-start gap-3">
                      <Lightbulb className="h-5 w-5 text-blue-500 mt-0.5" />
                      <div>
                        <h4 className="font-medium text-blue-900 dark:text-blue-100">Intelligent Suggestions</h4>
                        <p className="text-sm text-blue-700 dark:text-blue-300 mt-1">
                          Form fields have been pre-filled based on the previous bill from <strong>{autoGeneratedBill.vendor || 'this vendor'}</strong>.
                          {autoGeneratedBill.schedulePayment && (
                            <> The suggested start date is based on your <strong>{autoGeneratedBill.schedulePayment}</strong> schedule.</>  
                          )}
                        </p>
                        {aiExtractionData && (
                          <Badge variant="outline" className="mt-2">
                            Enhanced with AI analysis ({Math.round((aiExtractionData.confidence || 0) * 100)}% confidence)
                          </Badge>
                        )}
                      </div>
                    </div>
                  </CardContent>
                </Card>
              )}
            </TabsContent>
          </Tabs>

          {/* Bill Form */}
          <Form {...form}>
            <form onSubmit={form.handleSubmit(onSubmit)} className="space-y-4">
              <div className="grid grid-cols-2 gap-4">
                {/* Title */}
                <FormField
                  control={form.control}
                  name="title"
                  render={({ field }) => (
                    <FormItem>
                      <FormLabel>Title *</FormLabel>
                      <FormControl>
                        <Input 
                          {...field} 
                          placeholder={autoGeneratedBill?.title ? `Suggested: ${autoGeneratedBill.title}` : 'Enter bill title'}
                          data-testid="input-bill-title" 
                        />
                      </FormControl>
                      <FormMessage />
                    </FormItem>
                  )}
                />

                {/* Category */}
                <FormField
                  control={form.control}
                  name="category"
                  render={({ field }) => (
                    <FormItem>
                      <FormLabel>Category *</FormLabel>
                      <Select onValueChange={field.onChange} value={field.value}>
                        <FormControl>
                          <SelectTrigger data-testid="select-category">
                            <SelectValue placeholder="Select category" />
                          </SelectTrigger>
                        </FormControl>
                        <SelectContent>
                          <SelectItem value="insurance">Insurance</SelectItem>
                          <SelectItem value="maintenance">Maintenance</SelectItem>
                          <SelectItem value="salary">Salary</SelectItem>
                          <SelectItem value="utilities">Utilities</SelectItem>
                          <SelectItem value="cleaning">Cleaning</SelectItem>
                          <SelectItem value="security">Security</SelectItem>
                          <SelectItem value="landscaping">Landscaping</SelectItem>
                          <SelectItem value="professional_services">Professional Services</SelectItem>
                          <SelectItem value="administration">Administration</SelectItem>
                          <SelectItem value="repairs">Repairs</SelectItem>
                          <SelectItem value="supplies">Supplies</SelectItem>
                          <SelectItem value="taxes">Taxes</SelectItem>
                          <SelectItem value="technology">Technology</SelectItem>
                          <SelectItem value="reserves">Reserves</SelectItem>
                          <SelectItem value="other">Other</SelectItem>
                        </SelectContent>
                      </Select>
                      <FormMessage />
                    </FormItem>
                  )}
                />

                {/* Vendor */}
                <FormField
                  control={form.control}
                  name="vendor"
                  render={({ field }) => (
                    <FormItem>
                      <FormLabel>Vendor</FormLabel>
                      <FormControl>
                        <Input 
                          {...field} 
                          placeholder={autoGeneratedBill?.vendor ? `Suggested: ${autoGeneratedBill.vendor}` : 'Enter vendor name'}
                          data-testid="input-vendor" 
                        />
                      </FormControl>
                      <FormMessage />
                    </FormItem>
                  )}
                />

                {/* Total Amount */}
                <FormField
                  control={form.control}
                  name="totalAmount"
                  render={({ field }) => (
                    <FormItem>
                      <FormLabel>Total Amount *</FormLabel>
                      <FormControl>
                        <Input 
                          placeholder={autoGeneratedBill?.totalAmount ? `Suggested: $${autoGeneratedBill.totalAmount}` : '0.00'}
                          type="number" 
                          step="0.01" 
                          {...field}
                          data-testid="input-total-amount"
                        />
                      </FormControl>
                      <FormMessage />
                    </FormItem>
                  )}
                />

                {/* Start Date */}
                <FormField
                  control={form.control}
                  name="startDate"
                  render={({ field }) => (
                    <FormItem>
                      <FormLabel className="flex items-center gap-2">
                        <Calendar className="h-4 w-4" />
                        Start Date *
                        {autoGeneratedBill?.schedulePayment && (
                          <Badge variant="outline" className="text-xs">
                            Next {autoGeneratedBill.schedulePayment} period
                          </Badge>
                        )}
                      </FormLabel>
                      <FormControl>
                        <Input 
                          type="date" 
                          {...field}
                          data-testid="input-start-date"
                        />
                      </FormControl>
                      <FormDescription className="text-xs">
                        {autoGeneratedBill?.schedulePayment 
                          ? `Suggested based on ${autoGeneratedBill.schedulePayment} schedule`
                          : 'Select the bill start date'
                        }
                      </FormDescription>
                      <FormMessage />
                    </FormItem>
                  )}
                />

                {/* End Date */}
                <FormField
                  control={form.control}
                  name="endDate"
                  render={({ field }) => (
                    <FormItem>
                      <FormLabel>End Date</FormLabel>
                      <FormControl>
                        <Input 
                          type="date" 
                          {...field}
                          data-testid="input-end-date"
                        />
                      </FormControl>
                      <FormMessage />
                    </FormItem>
                  )}
                />
              </div>

              {/* Description */}
              <FormField
                control={form.control}
                name="description"
                render={({ field }) => (
                  <FormItem>
                    <FormLabel>Description</FormLabel>
                    <FormControl>
                      <Textarea 
                        {...field} 
                        rows={3}
                        data-testid="textarea-description"
                      />
                    </FormControl>
                    <FormMessage />
                  </FormItem>
                )}
              />

              {/* Notes */}
              <FormField
                control={form.control}
                name="notes"
                render={({ field }) => (
                  <FormItem>
                    <FormLabel>Notes</FormLabel>
                    <FormControl>
                      <Textarea 
                        {...field} 
                        rows={2}
                        data-testid="textarea-notes"
                      />
                    </FormControl>
                    <FormMessage />
                  </FormItem>
                )}
              />

              {/* Document Management Section */}
              <div className="space-y-4">
                <Separator />
                
                <div className="flex items-center justify-between">
                  <div className="flex items-center gap-2">
                    <Paperclip className="h-5 w-5 text-gray-500" />
                    <h3 className="text-lg font-semibold">Document Attachments</h3>
                    {attachedFiles.length > 0 && (
                      <Badge variant="secondary" data-testid="badge-attachment-count">
                        {attachedFiles.length} file{attachedFiles.length !== 1 ? 's' : ''}
                      </Badge>
                    )}
                  </div>
                  <Button
                    type="button"
                    variant="outline"
                    size="sm"
                    onClick={() => document.getElementById('manual-file-input')?.click()}
                    data-testid="button-add-files"
                    className="flex items-center gap-2"
                  >
                    <Plus className="h-4 w-4" />
                    Add Files
                  </Button>
                </div>

                {/* Hidden file input for manual uploads */}
                <input
                  id="manual-file-input"
                  type="file"
                  multiple
                  accept=".pdf,.doc,.docx,.xls,.xlsx,.jpg,.jpeg,.png"
                  style={{ display: 'none' }}
                  onChange={handleManualFileUpload}
                />

                {/* Attached Files Display */}
                {attachedFiles.length > 0 && (
                  <div className="space-y-3">
                    <div className="grid gap-3">
                      {attachedFiles.map((attachment) => (
                        <Card key={attachment.id} className="p-3" data-testid={`attachment-${attachment.id}`}>
                          <div className="flex items-center gap-3">
                            {/* File Icon/Preview */}
                            <div className="flex-shrink-0">
                              {attachment.preview ? (
                                <div className="relative w-12 h-12 rounded border overflow-hidden">
                                  <img 
                                    src={attachment.preview} 
                                    alt={attachment.file.name}
                                    className="w-full h-full object-cover"
                                  />
                                </div>
                              ) : (
                                <div className="w-12 h-12 bg-gray-100 dark:bg-gray-800 rounded border flex items-center justify-center">
                                  <File className="h-6 w-6 text-gray-500" />
                                </div>
                              )}
                            </div>

                            {/* File Info */}
                            <div className="flex-1 min-w-0">
                              <div className="flex items-center gap-2 mb-1">
                                <p className="text-sm font-medium text-gray-900 dark:text-gray-100 truncate">
                                  {attachment.file.name}
                                </p>
                                {attachment.aiAnalyzed && (
                                  <Badge variant="outline" className="flex items-center gap-1 text-xs">
                                    <Sparkles className="h-3 w-3" />
                                    AI Analyzed
                                  </Badge>
                                )}
                                <Badge variant="secondary" className="text-xs capitalize">
                                  {attachment.category}
                                </Badge>
                              </div>
                              
                              <div className="flex items-center gap-4 text-xs text-gray-500 dark:text-gray-400">
                                <span>{(attachment.file.size / 1024 / 1024).toFixed(2)} MB</span>
                                <span className="capitalize">{attachment.file.type.split('/')[1]}</span>
                                
                                {/* Upload Progress */}
                                {uploadProgress[attachment.id] !== undefined && uploadProgress[attachment.id] < 100 && (
                                  <div className="flex items-center gap-2 flex-1">
                                    <Progress 
                                      value={uploadProgress[attachment.id]} 
                                      className="h-2 flex-1"
                                    />
                                    <span>{Math.round(uploadProgress[attachment.id])}%</span>
                                  </div>
                                )}
                                
                                {uploadProgress[attachment.id] === 100 && (
                                  <div className="flex items-center gap-1 text-green-600 dark:text-green-400">
                                    <CheckCircle className="h-3 w-3" />
                                    <span>Ready</span>
                                  </div>
                                )}
                              </div>
                            </div>

                            {/* File Actions */}
                            <div className="flex items-center gap-1">
                              {attachment.preview && (
                                <Button
                                  type="button"
                                  variant="ghost"
                                  size="sm"
                                  onClick={() => window.open(attachment.preview, '_blank')}
                                  data-testid={`button-preview-${attachment.id}`}
                                  title="Preview file"
                                >
                                  <Eye className="h-4 w-4" />
                                </Button>
                              )}
                              <Button
                                type="button"
                                variant="ghost"
                                size="sm"
                                onClick={() => removeFileFromAttachments(attachment.id)}
                                data-testid={`button-remove-${attachment.id}`}
                                title="Remove file"
                                className="text-red-500 hover:text-red-700 hover:bg-red-50 dark:hover:bg-red-950"
                              >
                                <Trash2 className="h-4 w-4" />
                              </Button>
                            </div>
                          </div>
                        </Card>
                      ))}
                    </div>
                  </div>
                )}

                {/* No Files State */}
                {attachedFiles.length === 0 && (
                  <div 
                    className="border-2 border-dashed border-gray-300 dark:border-gray-600 rounded-lg p-6 text-center cursor-pointer hover:border-gray-400 dark:hover:border-gray-500 transition-colors"
                    onClick={() => document.getElementById('manual-file-input')?.click()}
                    data-testid="drop-zone-empty"
                  >
                    <Upload className="h-8 w-8 text-gray-400 mx-auto mb-3" />
                    <p className="text-sm text-gray-600 dark:text-gray-400 mb-2">
                      No documents attached yet
                    </p>
                    <p className="text-xs text-gray-500 dark:text-gray-500">
                      Click here or use the "Add Files" button to attach documents to this bill
                    </p>
                  </div>
                )}

                {/* Attachment Summary */}
                {attachedFiles.length > 0 && (
                  <div className="bg-blue-50 dark:bg-blue-950 border border-blue-200 dark:border-blue-800 rounded-lg p-3">
                    <div className="flex items-start gap-2">
                      <CheckCircle className="h-4 w-4 text-blue-500 mt-0.5" />
                      <div className="text-sm text-blue-800 dark:text-blue-200">
                        <p className="font-medium">
                          {attachedFiles.length} document{attachedFiles.length !== 1 ? 's' : ''} ready to attach
                        </p>
                        <p className="text-xs mt-1">
                          {aiExtractionData ? 
                            'AI-analyzed documents will help pre-fill bill information and be attached to the final bill.' :
                            'These documents will be attached to the bill when created.'
                          }
                        </p>
                      </div>
                    </div>
                  </div>
                )}
              </div>

              {/* Action Buttons */}
              <div className="flex justify-between pt-4">
                <Button
                  type="button"
                  variant="outline"
                  onClick={handleClose}
                  data-testid="button-cancel"
                >
                  Cancel
                </Button>
                <Button
                  type="submit"
                  disabled={createBillMutation.isPending}
                  data-testid="button-create-bill"
                  className="min-w-[120px]"
                >
                  {createBillMutation.isPending ? (
                    <>
                      <Sparkles className="h-4 w-4 mr-2 animate-spin" />
                      Creating...
                    </>
                  ) : (
                    'Create Bill'
                  )}
                </Button>
              </div>
            </form>
          </Form>

          {/* Workflow Info */}
          <div className="bg-amber-50 dark:bg-amber-950 border border-amber-200 dark:border-amber-800 rounded-lg p-3">
            <p className="text-sm text-amber-800 dark:text-amber-200">
              <strong>Smart Workflow:</strong> This bill is being created from your recurring template. 
              Creating it will automatically update the original bill to prevent duplicates and maintain accurate budgeting.
            </p>
          </div>
          
          {/* File Upload Notice */}
          {uploadedFile && (
            <div className="bg-blue-50 dark:bg-blue-950 border border-blue-200 dark:border-blue-800 rounded-lg p-3">
              <p className="text-sm text-blue-800 dark:text-blue-200">
                <strong>Note:</strong> The uploaded file ({uploadedFile.name}) has been used for AI analysis but will not be automatically attached to this bill. 
                You can manually attach documents to the bill after creation using the document management feature.
              </p>
            </div>
          )}
          
          {/* Hidden AI Extraction Component */}
          {uploadedFile && (
            <GeminiBillExtractor
              file={uploadedFile}
              onExtractionComplete={handleAiExtractionComplete}
            />
          )}
        </div>
      </DialogContent>
    </Dialog>
  );
}