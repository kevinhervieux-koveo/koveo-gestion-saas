interface AutoGeneratedBillWorkflowProps {
  autoGeneratedBill: Bill | null;
  isOpen: boolean;
  onClose: () => void;
  onSuccess?: (billId: string) => void;
}

const newBillSchema = z.object({
  title: z.string().min(1, 'Title is required').max(200, 'Title must be less than 200 characters'),
  description: z.string().max(1000, 'Description must be less than 1000 characters').optional(),
  category: z.enum([
    'insurance',
    'maintenance',
    'salary',
    'utilities',
    'cleaning',
    'security',
    'landscaping',
    'professional_services',
    'administration',
    'repairs',
    'supplies',
    'taxes',
    'technology',
    'reserves',
    'other',
  ]),
  vendor: z.string().max(150, 'Vendor name must be less than 150 characters').optional(),
  paymentType: z.enum(['unique', 'recurrent']),
  schedulePayment: z.enum(['weekly', 'monthly', 'quarterly', 'yearly', 'custom']).optional(),
  hasInitialPayment: z.boolean().optional(),
  recurringPaymentsEqual: z.boolean().optional(),
  initialPaymentAmount: z.string().optional().refine((val) => {
    if (!val) return true;
    const num = parseFloat(val);
    return !isNaN(num) && num > 0 && num <= 999999.99;
  }, 'Initial payment amount must be between $0.01 and $999,999.99'),
  recurringPaymentAmount: z.string().optional().refine((val) => {
    if (!val) return true;
    const num = parseFloat(val);
    return !isNaN(num) && num > 0 && num <= 999999.99;
  }, 'Recurring payment amount must be between $0.01 and $999,999.99'),
  customPayments: z.array(z.object({
    amount: z.string().min(1, 'Amount is required').refine((val) => {
      const num = parseFloat(val);
      return !isNaN(num) && num > 0 && num <= 999999.99;
    }, 'Amount must be between $0.01 and $999,999.99'),
    date: z.string().min(1, 'Date is required').refine((val) => {
      return !isNaN(Date.parse(val));
    }, 'Date must be a valid date'),
    description: z.string().optional()
  })).optional(),
  totalAmount: z.string().optional().refine((val) => {
    if (!val) return true;
    const num = parseFloat(val);
    return !isNaN(num) && num > 0 && num <= 999999.99;
  }, 'Total amount must be between $0.01 and $999,999.99'),
  startDate: z.string().min(1, 'Start date is required').refine((val) => {
    return !isNaN(Date.parse(val));
  }, 'Start date must be a valid date'),
  endDate: z.string().optional().refine((val) => {
    if (!val) return true;
    return !isNaN(Date.parse(val));
  }, 'End date must be a valid date'),
  status: z.enum(['draft', 'sent', 'overdue', 'paid', 'cancelled']),
  notes: z.string().max(2000, 'Notes must be less than 2000 characters').optional(),
}).superRefine((data, ctx) => {
  // Custom validation logic for payment structure with specific field error targeting
  if (data.paymentType === 'unique') {
    // For unique payments, total amount is required
    if (!data.totalAmount || data.totalAmount.trim() === '') {
      ctx.addIssue({
        code: z.ZodIssueCode.custom,
        message: 'Total amount is required for one-time bills',
        path: ['totalAmount']
      });
    }
  } else if (data.paymentType === 'recurrent') {
    // For recurring payments, validate based on configuration
    if (data.hasInitialPayment && (!data.initialPaymentAmount || data.initialPaymentAmount.trim() === '')) {
      ctx.addIssue({
        code: z.ZodIssueCode.custom,
        message: 'Initial payment amount is required when initial payment is enabled',
        path: ['initialPaymentAmount']
      });
    }
    if (data.recurringPaymentsEqual && (!data.recurringPaymentAmount || data.recurringPaymentAmount.trim() === '')) {
      ctx.addIssue({
        code: z.ZodIssueCode.custom,
        message: 'Recurring payment amount is required for equal recurring payments',
        path: ['recurringPaymentAmount']
      });
    }
    if (!data.recurringPaymentsEqual && (!data.customPayments || data.customPayments.length === 0)) {
      ctx.addIssue({
        code: z.ZodIssueCode.custom,
        message: 'Custom payments are required when recurring payments are not equal',
        path: ['customPayments']
      });
    }
  }
});

type NewBillFormData = z.infer<typeof newBillSchema>;

export default function AutoGeneratedBillWorkflow({
  autoGeneratedBill,
  isOpen,
  onClose,
  onSuccess,
}: AutoGeneratedBillWorkflowProps) {
  const { t } = useLanguage();
  const { toast } = useToast();
  const queryClient = useQueryClient();
  
  // AI Analysis state
  const [uploadedFile, setUploadedFile] = useState<File | null>(null);
  const [attachedDocument, setAttachedDocument] = useState<Document | null>(null);
  const [aiExtractionLoading, setAiExtractionLoading] = useState(false);
  const [aiExtractionData, setAiExtractionData] = useState<any>(null);
  const [activeTab, setActiveTab] = useState<'manual' | 'ai'>('manual');
  
  // Document Management state
  const [attachedFiles, setAttachedFiles] = useState<AttachedFile[]>([]);
  const [uploadProgress, setUploadProgress] = useState<{ [key: string]: number }>({});
  const [showDocumentSection, setShowDocumentSection] = useState(false);

  // Helper function to generate intelligent suggestions
  const generateSuggestions = (bill: Bill | null) => {
    if (!bill) return {
      paymentType: 'unique' as const,
      status: 'draft' as const,
    };
    
    // Generate next period date suggestions
    const nextPeriodDate = new Date();
    if (bill.schedulePayment === 'monthly') {
      nextPeriodDate.setMonth(nextPeriodDate.getMonth() + 1);
    } else if (bill.schedulePayment === 'quarterly') {
      nextPeriodDate.setMonth(nextPeriodDate.getMonth() + 3);
    } else if (bill.schedulePayment === 'yearly') {
      nextPeriodDate.setFullYear(nextPeriodDate.getFullYear() + 1);
    } else {
      nextPeriodDate.setMonth(nextPeriodDate.getMonth() + 1); // Default to monthly
    }
    
    // Pre-fill payment configuration based on template bill
    const hasMultiplePayments = bill.costs && bill.costs.length > 1;
    const hasInitialPayment = hasMultiplePayments && bill.costs[0] !== bill.costs[1];
    const recurringPaymentsEqual = hasMultiplePayments && 
      bill.costs.slice(1).every(cost => cost === bill.costs[1]);
    
    return {
      title: bill.title || '',
      description: bill.description || '',
      category: bill.category || 'other',
      vendor: bill.vendor || '',
      paymentType: bill.paymentType || 'unique',
      schedulePayment: bill.schedulePayment || 'monthly',
      hasInitialPayment: hasInitialPayment,
      recurringPaymentsEqual: recurringPaymentsEqual,
      initialPaymentAmount: hasInitialPayment ? bill.costs[0]?.toString() : '',
      recurringPaymentAmount: (hasMultiplePayments && recurringPaymentsEqual) 
        ? bill.costs[1]?.toString() 
        : '',
      totalAmount: bill.totalAmount?.toString() || '',
      startDate: nextPeriodDate.toISOString().split('T')[0],
      endDate: bill.endDate || '',
      status: 'draft' as const,
      notes: bill.notes || '',
    };
  };

  // Form setup with intelligent suggestions from auto-generated bill
  const form = useForm<NewBillFormData>({
    resolver: zodResolver(newBillSchema),
    defaultValues: generateSuggestions(autoGeneratedBill)
  });
  
  // Update form when autoGeneratedBill changes
  useEffect(() => {
    if (autoGeneratedBill) {
      const suggestions = generateSuggestions(autoGeneratedBill);
      Object.entries(suggestions).forEach(([key, value]) => {
        if (value !== undefined && value !== null) {
          form.setValue(key as keyof NewBillFormData, value);
        }
      });
    }
  }, [autoGeneratedBill, form]);

  // Handle AI extraction results
  const handleAiExtractionComplete = (data: {
    success: boolean;
    formData?: any;
    confidence?: number;
    error?: string;
    rawData?: any;
    isLoading?: boolean;
  }) => {
    if (data.isLoading) {
      setAiExtractionLoading(true);
      return;
    }
    
    setAiExtractionLoading(false);
    
    if (data.success && data.formData) {
      setAiExtractionData(data);
      
      // Auto-populate form with AI-extracted data
      Object.entries(data.formData).forEach(([key, value]) => {
        if (value !== undefined && value !== null && value !== '') {
          if (key === 'totalAmount' && typeof value === 'number') {
            form.setValue(key as keyof NewBillFormData, value.toString());
          } else if (value && typeof value === 'string') {
            form.setValue(key as keyof NewBillFormData, value);
          }
        }
      });
      
      toast({
        title: 'AI Extraction Complete',
        description: `Extracted bill data with ${Math.round((data.confidence || 0) * 100)}% confidence`,
      });
      
      // Switch to manual tab to show extracted data
      setActiveTab('manual');
    } else if (data.error) {
      toast({
        title: 'AI Extraction Failed',
        description: data.error,
        variant: 'destructive',
      });
    }
  };
  
  // Handle AI file upload
  const handleDocumentChange = (file: File | null, text: string | null) => {
    setUploadedFile(file);
    
    if (file) {
      // Add file to attachments when uploaded for AI analysis
      addFileToAttachments(file, true);
    }
    
    if (text) {
      // Handle text input - could be enhanced with text analysis
      form.setValue('description', text);
    }
  };

  
  // Add file to attachments list
  const addFileToAttachments = useCallback((file: File, aiAnalyzed = false) => {
    const fileId = `${Date.now()}-${Math.random().toString(36).substr(2, 9)}`;
    const isImage = file.type.startsWith('image/');
    
    const newAttachment = {
      id: fileId,
      file,
      preview: isImage ? URL.createObjectURL(file) : undefined,
      uploadProgress: 0,
      aiAnalyzed,
      category: getCategoryFromFileName(file.name)
    };
    
    setAttachedFiles(prev => [...prev, newAttachment]);
    setShowDocumentSection(true);
    
    // Simulate upload progress
    let progress = 0;
    const interval = setInterval(() => {
      progress += Math.random() * 30;
      if (progress >= 100) {
        progress = 100;
        clearInterval(interval);
      }
      setUploadProgress(prev => ({ ...prev, [fileId]: progress }));
    }, 200);
  }, []);
  
  // Remove file from attachments
  const removeFileFromAttachments = useCallback((fileId: string) => {
    setAttachedFiles(prev => {
      const file = prev.find(f => f.id === fileId);
      if (file?.preview) {
        URL.revokeObjectURL(file.preview);
      }
      return prev.filter(f => f.id !== fileId);
    });
    setUploadProgress(prev => {
      const { [fileId]: removed, ...rest } = prev;
      return rest;
    });
  }, []);
  
  // Get category from file name
  const getCategoryFromFileName = (fileName: string): string => {
    const lower = fileName.toLowerCase();
    if (lower.includes('invoice') || lower.includes('bill')) return 'invoice';
    if (lower.includes('receipt')) return 'receipt';
    if (lower.includes('contract')) return 'contract';
    if (lower.includes('quote') || lower.includes('estimate')) return 'quote';
    return 'document';
  };
  

  // Create bill from auto-generated template mutation
  const createBillMutation = useMutation({
    mutationFn: async (formData: NewBillFormData) => {
      // Create FormData to handle both bill data and file uploads
      const formDataToSend = new FormData();
      
      // Add bill data
      formDataToSend.append('templateId', autoGeneratedBill?.id || '');
      formDataToSend.append('suggestedDate', formData.startDate);
      
      // Calculate costs array based on payment structure
      let costs: string[] = [];
      let calculatedTotalAmount = formData.totalAmount;
      
      if (formData.paymentType === 'unique') {
        costs = [formData.totalAmount || '0'];
      } else if (formData.paymentType === 'recurrent') {
        if (formData.recurringPaymentsEqual) {
          // For equal recurring payments - generate all 12 payment amounts
          const maxPayments = 12; // Default to 12 monthly payments
          
          if (formData.hasInitialPayment && formData.initialPaymentAmount) {
            // First payment is the initial amount
            costs.push(formData.initialPaymentAmount);
            
            // Remaining payments use the recurring amount
            if (formData.recurringPaymentAmount) {
              for (let i = 1; i < maxPayments; i++) {
                costs.push(formData.recurringPaymentAmount);
              }
            }
          } else if (formData.recurringPaymentAmount) {
            // All payments use the same recurring amount
            for (let i = 0; i < maxPayments; i++) {
              costs.push(formData.recurringPaymentAmount);
            }
          }
        } else if (formData.customPayments && formData.customPayments.length > 0) {
          // For custom amounts
          costs = formData.customPayments.map(p => p.amount).filter(a => a && a.trim() !== '');
        }
        
        // Calculate total if not provided
        if (!calculatedTotalAmount || calculatedTotalAmount.trim() === '') {
          const total = costs.reduce((sum, cost) => sum + parseFloat(cost || '0'), 0);
          calculatedTotalAmount = total.toString();
        }
      }
      
      // Extract custom schedule dates for backend persistence
      let scheduleCustom: string[] = [];
      if (formData.paymentType === 'recurrent' && !formData.recurringPaymentsEqual && formData.customPayments) {
        scheduleCustom = formData.customPayments
          .map(p => p.date)
          .filter(d => d && d.trim() !== '');
      }

      const newBillData = {
        title: formData.title,
        description: formData.description,
        category: formData.category,
        vendor: formData.vendor,
        paymentType: formData.paymentType,
        schedulePayment: formData.schedulePayment || null,
        scheduleCustom: scheduleCustom,
        costs: costs,
        totalAmount: calculatedTotalAmount ? parseFloat(calculatedTotalAmount) : parseFloat(formData.totalAmount || '0'),
        startDate: formData.startDate,
        endDate: formData.endDate || null,
        status: formData.status,
        notes: formData.notes,
        isAiAnalyzed: !!aiExtractionData,
        aiAnalysisData: aiExtractionData || null,
        // Include the new payment structure fields
        paymentStructure: {
          hasInitialPayment: formData.hasInitialPayment,
          recurringPaymentsEqual: formData.recurringPaymentsEqual,
          initialPaymentAmount: formData.initialPaymentAmount,
          recurringPaymentAmount: formData.recurringPaymentAmount,
          customPayments: formData.customPayments,
        },
      };
      
      formDataToSend.append('newBillData', JSON.stringify(newBillData));
      
      // Add attached files
      attachedFiles.forEach((attachment, index) => {
        formDataToSend.append(`files`, attachment.file);
        formDataToSend.append(`fileMetadata_${index}`, JSON.stringify({
          category: attachment.category,
          aiAnalyzed: attachment.aiAnalyzed
        }));
      });
      
      // Send request with FormData (no JSON headers needed)
      const response = await fetch('/api/bills/from-auto-generated', {
        method: 'POST',
        body: formDataToSend,
        credentials: 'include'
      });
      
      if (!response.ok) {
        const errorData = await response.json().catch(() => ({}));
        throw new Error(errorData.message || `HTTP error! status: ${response.status}`);
      }
      
      return await response.json();
    },
    onSuccess: (responseData: any) => {
      queryClient.invalidateQueries({ queryKey: ['/api/bills'] });
      queryClient.invalidateQueries({ queryKey: ['/api/bills/auto-generated'] });
      
      toast({
        title: 'Success',
        description: 'Bill created successfully from auto-generated template',
      });
      
      onSuccess?.(responseData.bill?.id);
      handleClose();
    },
    onError: (error: any) => {
      toast({
        title: 'Error',
        description: error.message || 'Failed to create bill from template',
        variant: 'destructive',
      });
    }
  });
  
  // Handle dialog close with cleanup
  const handleClose = () => {
    form.reset();
    setUploadedFile(null);
    setAttachedDocument(null);
    setAiExtractionData(null);
    setAiExtractionLoading(false);
    setActiveTab('manual');
    
    // Clean up attached files and their preview URLs
    attachedFiles.forEach(file => {
      if (file.preview) {
        URL.revokeObjectURL(file.preview);
      }
    });
    setAttachedFiles([]);
    setUploadProgress({});
    setShowDocumentSection(false);
    
    onClose();
  };


  const onSubmit = (data: NewBillFormData) => {
    createBillMutation.mutate(data);
  };

  if (!autoGeneratedBill) return null;

  return (
    <Dialog open={isOpen} onOpenChange={onClose}>
      <DialogContent className="max-w-2xl max-h-[90vh] overflow-y-auto">
        <DialogHeader>
          <DialogTitle className="flex items-center gap-2">
            <Sparkles className="h-5 w-5 text-blue-500" />
            Create Bill from Auto-Generated Template
          </DialogTitle>
        </DialogHeader>

        <div className="space-y-6">
          {/* Document Attachments Section */}
          <StandardDocumentAttachments
            onDocumentChange={handleDocumentChange}
            attachedFiles={attachedFiles}
            onRemoveFile={removeFileFromAttachments}
            uploadProgress={uploadProgress}
            uploadContext={{
              type: 'bills',
              buildingId: autoGeneratedBill?.buildingId
            }}
            allowedFileTypes={[
              'application/pdf',
              'image/jpeg',
              'image/jpg', 
              'image/png',
              'application/msword',
              'application/vnd.openxmlformats-officedocument.wordprocessingml.document',
              'application/vnd.ms-excel',
              'application/vnd.openxmlformats-officedocument.spreadsheetml.sheet'
            ]}
            maxFileSize={25}
            aiEnabled={true}
            onAiExtractionComplete={handleAiExtractionComplete}
            aiExtractionLoading={aiExtractionLoading}
            aiExtractionData={aiExtractionData}
            showAiToggle={false}
            title="Document Attachments"
            showUploadTabs={true}
            defaultUploadTab="file"
            showSection={true}
          />

          {/* Intelligent Suggestions Header */}
          {autoGeneratedBill && (
            <Card className="bg-blue-50 dark:bg-blue-950 border-blue-200 dark:border-blue-800">
              <CardContent className="pt-4">
                <div className="flex items-start gap-3">
                  <Lightbulb className="h-5 w-5 text-blue-500 mt-0.5" />
                  <div>
                    <h4 className="font-medium text-blue-900 dark:text-blue-100">Intelligent Suggestions</h4>
                    <p className="text-sm text-blue-700 dark:text-blue-300 mt-1">
                      Form fields have been pre-filled based on the previous bill from <strong>{autoGeneratedBill.vendor || 'this vendor'}</strong>.
                      {autoGeneratedBill.schedulePayment && (
                        <> The suggested start date is based on your <strong>{autoGeneratedBill.schedulePayment}</strong> schedule.</>  
                      )}
                    </p>
                    {aiExtractionData && (
                      <Badge variant="outline" className="mt-2">
                        Enhanced with AI analysis ({Math.round((aiExtractionData.confidence || 0) * 100)}% confidence)
                      </Badge>
                    )}
                  </div>
                </div>
              </CardContent>
            </Card>
          )}

          {/* Bill Form */}
          <Form {...form}>
            <form onSubmit={form.handleSubmit(onSubmit)} className="space-y-4">
              <div className="grid grid-cols-2 gap-4">
                {/* Title */}
                <FormField
                  control={form.control}
                  name="title"
                  render={({ field }) => (
                    <FormItem>
                      <FormLabel>Title *</FormLabel>
                      <FormControl>
                        <Input 
                          {...field} 
                          placeholder={autoGeneratedBill?.title ? `Suggested: ${autoGeneratedBill.title}` : 'Enter bill title'}
                          data-testid="input-bill-title" 
                        />
                      </FormControl>
                      <FormMessage />
                    </FormItem>
                  )}
                />

                {/* Category */}
                <FormField
                  control={form.control}
                  name="category"
                  render={({ field }) => (
                    <FormItem>
                      <FormLabel>Category *</FormLabel>
                      <Select onValueChange={field.onChange} value={field.value}>
                        <FormControl>
                          <SelectTrigger data-testid="select-category">
                            <SelectValue placeholder="Select category" />
                          </SelectTrigger>
                        </FormControl>
                        <SelectContent>
                          <SelectItem value="insurance">Insurance</SelectItem>
                          <SelectItem value="maintenance">Maintenance</SelectItem>
                          <SelectItem value="salary">Salary</SelectItem>
                          <SelectItem value="utilities">Utilities</SelectItem>
                          <SelectItem value="cleaning">Cleaning</SelectItem>
                          <SelectItem value="security">Security</SelectItem>
                          <SelectItem value="landscaping">Landscaping</SelectItem>
                          <SelectItem value="professional_services">Professional Services</SelectItem>
                          <SelectItem value="administration">Administration</SelectItem>
                          <SelectItem value="repairs">Repairs</SelectItem>
                          <SelectItem value="supplies">Supplies</SelectItem>
                          <SelectItem value="taxes">Taxes</SelectItem>
                          <SelectItem value="technology">Technology</SelectItem>
                          <SelectItem value="reserves">Reserves</SelectItem>
                          <SelectItem value="other">Other</SelectItem>
                        </SelectContent>
                      </Select>
                      <FormMessage />
                    </FormItem>
                  )}
                />

                {/* Vendor */}
                <FormField
                  control={form.control}
                  name="vendor"
                  render={({ field }) => (
                    <FormItem>
                      <FormLabel>Vendor</FormLabel>
                      <FormControl>
                        <Input 
                          {...field} 
                          placeholder={autoGeneratedBill?.vendor ? `Suggested: ${autoGeneratedBill.vendor}` : 'Enter vendor name'}
                          data-testid="input-vendor" 
                        />
                      </FormControl>
                      <FormMessage />
                    </FormItem>
                  )}
                />

                {/* Payment Type */}
                <FormField
                  control={form.control}
                  name="paymentType"
                  render={({ field }) => (
                    <FormItem>
                      <FormLabel>Payment Type *</FormLabel>
                      <Select onValueChange={field.onChange} value={field.value}>
                        <FormControl>
                          <SelectTrigger data-testid="select-payment-type">
                            <SelectValue placeholder="Select payment type" />
                          </SelectTrigger>
                        </FormControl>
                        <SelectContent>
                          <SelectItem value="unique">One-Time Bill</SelectItem>
                          <SelectItem value="recurrent">Recurring Payment</SelectItem>
                        </SelectContent>
                      </Select>
                      <FormMessage />
                    </FormItem>
                  )}
                />

                {/* Schedule Payment (only for recurrent) */}
                {form.watch('paymentType') === 'recurrent' && (
                  <FormField
                    control={form.control}
                    name="schedulePayment"
                    render={({ field }) => (
                      <FormItem>
                        <FormLabel>Payment Schedule *</FormLabel>
                        <Select onValueChange={field.onChange} value={field.value}>
                          <FormControl>
                            <SelectTrigger data-testid="select-schedule-payment">
                              <SelectValue placeholder="Select schedule" />
                            </SelectTrigger>
                          </FormControl>
                          <SelectContent>
                            <SelectItem value="weekly">Weekly</SelectItem>
                            <SelectItem value="monthly">Monthly</SelectItem>
                            <SelectItem value="quarterly">Quarterly</SelectItem>
                            <SelectItem value="yearly">Yearly</SelectItem>
                            <SelectItem value="custom">Custom Schedule</SelectItem>
                          </SelectContent>
                        </Select>
                        <FormMessage />
                      </FormItem>
                    )}
                  />
                )}

                {/* Status */}
                <FormField
                  control={form.control}
                  name="status"
                  render={({ field }) => (
                    <FormItem>
                      <FormLabel>Status</FormLabel>
                      <Select onValueChange={field.onChange} value={field.value}>
                        <FormControl>
                          <SelectTrigger data-testid="select-status">
                            <SelectValue placeholder="Select status" />
                          </SelectTrigger>
                        </FormControl>
                        <SelectContent>
                          <SelectItem value="draft">Draft</SelectItem>
                          <SelectItem value="sent">Sent</SelectItem>
                          <SelectItem value="overdue">Overdue</SelectItem>
                          <SelectItem value="paid">Paid</SelectItem>
                          <SelectItem value="cancelled">Cancelled</SelectItem>
                        </SelectContent>
                      </Select>
                      <FormMessage />
                    </FormItem>
                  )}
                />

                {/* Total Amount */}
                <FormField
                  control={form.control}
                  name="totalAmount"
                  render={({ field }) => (
                    <FormItem>
                      <FormLabel>Total Amount *</FormLabel>
                      <FormControl>
                        <Input 
                          placeholder={autoGeneratedBill?.totalAmount ? `Suggested: $${autoGeneratedBill.totalAmount}` : '0.00'}
                          type="number" 
                          step="0.01" 
                          {...field}
                          data-testid="input-total-amount"
                        />
                      </FormControl>
                      <FormMessage />
                    </FormItem>
                  )}
                />

                {/* Start Date */}
                <FormField
                  control={form.control}
                  name="startDate"
                  render={({ field }) => (
                    <FormItem>
                      <FormLabel className="flex items-center gap-2">
                        <Calendar className="h-4 w-4" />
                        Start Date *
                        {autoGeneratedBill?.schedulePayment && (
                          <Badge variant="outline" className="text-xs">
                            Next {autoGeneratedBill.schedulePayment} period
                          </Badge>
                        )}
                      </FormLabel>
                      <FormControl>
                        <Input 
                          type="date" 
                          {...field}
                          data-testid="input-start-date"
                        />
                      </FormControl>
                      <FormDescription className="text-xs">
                        {autoGeneratedBill?.schedulePayment 
                          ? `Suggested based on ${autoGeneratedBill.schedulePayment} schedule`
                          : 'Select the bill start date'
                        }
                      </FormDescription>
                      <FormMessage />
                    </FormItem>
                  )}
                />

                {/* End Date */}
                <FormField
                  control={form.control}
                  name="endDate"
                  render={({ field }) => (
                    <FormItem>
                      <FormLabel>End Date</FormLabel>
                      <FormControl>
                        <Input 
                          type="date" 
                          {...field}
                          data-testid="input-end-date"
                        />
                      </FormControl>
                      <FormMessage />
                    </FormItem>
                  )}
                />
              </div>

              {/* Description */}
              <FormField
                control={form.control}
                name="description"
                render={({ field }) => (
                  <FormItem>
                    <FormLabel>Description</FormLabel>
                    <FormControl>
                      <Textarea 
                        {...field} 
                        rows={3}
                        data-testid="textarea-description"
                      />
                    </FormControl>
                    <FormMessage />
                  </FormItem>
                )}
              />

              {/* Payment Configuration - only for recurrent payments */}
              {form.watch('paymentType') === 'recurrent' && (
                <div className="space-y-4 p-4 border rounded-lg bg-gray-50 dark:bg-gray-900">
                  <h3 className="text-lg font-medium">Payment Configuration</h3>
                  
                  {/* Initial Payment Toggle */}
                  <div className="flex items-center space-x-2">
                    <FormField
                      control={form.control}
                      name="hasInitialPayment"
                      render={({ field }) => (
                        <FormItem className="flex flex-row items-start space-x-3 space-y-0">
                          <FormControl>
                            <input
                              type="checkbox"
                              checked={field.value}
                              onChange={field.onChange}
                              className="mt-1"
                              data-testid="checkbox-initial-payment"
                            />
                          </FormControl>
                          <div className="space-y-1 leading-none">
                            <FormLabel>Initial Payment</FormLabel>
                            <FormDescription>
                              Is there an upfront payment different from recurring amounts?
                            </FormDescription>
                          </div>
                        </FormItem>
                      )}
                    />
                  </div>

                  {/* Equal Recurring Payments Toggle */}
                  <div className="flex items-center space-x-2">
                    <FormField
                      control={form.control}
                      name="recurringPaymentsEqual"
                      render={({ field }) => (
                        <FormItem className="flex flex-row items-start space-x-3 space-y-0">
                          <FormControl>
                            <input
                              type="checkbox"
                              checked={field.value}
                              onChange={field.onChange}
                              className="mt-1"
                              data-testid="checkbox-recurring-equal"
                            />
                          </FormControl>
                          <div className="space-y-1 leading-none">
                            <FormLabel>Equal Recurring Payments</FormLabel>
                            <FormDescription>
                              Are all recurring payment amounts the same?
                            </FormDescription>
                          </div>
                        </FormItem>
                      )}
                    />
                  </div>

                  {/* Payment Amount Fields */}
                  <div className="grid grid-cols-2 gap-4">
                    {/* Initial Payment Amount */}
                    {form.watch('hasInitialPayment') && (
                      <FormField
                        control={form.control}
                        name="initialPaymentAmount"
                        render={({ field }) => (
                          <FormItem>
                            <FormLabel>Initial Payment Amount *</FormLabel>
                            <FormControl>
                              <Input 
                                placeholder="0.00"
                                type="number" 
                                step="0.01" 
                                {...field}
                                data-testid="input-initial-payment-amount"
                              />
                            </FormControl>
                            <FormDescription>Amount for the upfront payment</FormDescription>
                            <FormMessage />
                          </FormItem>
                        )}
                      />
                    )}

                    {/* Recurring Payment Amount */}
                    {form.watch('recurringPaymentsEqual') && (
                      <FormField
                        control={form.control}
                        name="recurringPaymentAmount"
                        render={({ field }) => (
                          <FormItem>
                            <FormLabel>Recurring Payment Amount *</FormLabel>
                            <FormControl>
                              <Input 
                                placeholder="0.00"
                                type="number" 
                                step="0.01" 
                                {...field}
                                data-testid="input-recurring-payment-amount"
                              />
                            </FormControl>
                            <FormDescription>Amount for each recurring payment</FormDescription>
                            <FormMessage />
                          </FormItem>
                        )}
                      />
                    )}
                  </div>

                  {/* Recurrence End Date */}
                  <FormField
                    control={form.control}
                    name="endDate"
                    render={({ field }) => (
                      <FormItem>
                        <FormLabel>Recurrence End Date (Optional)</FormLabel>
                        <FormControl>
                          <Input 
                            type="date" 
                            {...field}
                            data-testid="input-end-date-recurrence"
                          />
                        </FormControl>
                        <FormDescription>
                          Payment schedule will be limited to the next year. Setting an end date will stop recurring bills after this date.
                        </FormDescription>
                        <FormMessage />
                      </FormItem>
                    )}
                  />
                </div>
              )}

              {/* Notes */}
              <FormField
                control={form.control}
                name="notes"
                render={({ field }) => (
                  <FormItem>
                    <FormLabel>Notes</FormLabel>
                    <FormControl>
                      <Textarea 
                        {...field} 
                        rows={2}
                        data-testid="textarea-notes"
                      />
                    </FormControl>
                    <FormMessage />
                  </FormItem>
                )}
              />


              {/* Action Buttons */}
              <div className="flex justify-between pt-4">
                <Button
                  type="button"
                  variant="outline"
                  onClick={handleClose}
                  data-testid="button-cancel"
                >
                  Cancel
                </Button>
                <Button
                  type="submit"
                  disabled={createBillMutation.isPending}
                  data-testid="button-create-bill"
                  className="min-w-[120px]"
                >
                  {createBillMutation.isPending ? (
                    <>
                      <Sparkles className="h-4 w-4 mr-2 animate-spin" />
                      Creating...
                    </>
                  ) : (
                    'Create Bill'
                  )}
                </Button>
              </div>
            </form>
          </Form>

          {/* Workflow Info */}
          <div className="bg-amber-50 dark:bg-amber-950 border border-amber-200 dark:border-amber-800 rounded-lg p-3">
            <p className="text-sm text-amber-800 dark:text-amber-200">
              <strong>Smart Workflow:</strong> This bill is being created from your recurring template. 
              Creating it will automatically update the original bill to prevent duplicates and maintain accurate budgeting.
            </p>
          </div>
          
          {/* File Upload Notice */}
          {uploadedFile && (
            <div className="bg-blue-50 dark:bg-blue-950 border border-blue-200 dark:border-blue-800 rounded-lg p-3">
              <p className="text-sm text-blue-800 dark:text-blue-200">
                <strong>Note:</strong> The uploaded file ({uploadedFile.name}) has been used for AI analysis but will not be automatically attached to this bill. 
                You can manually attach documents to the bill after creation using the document management feature.
              </p>
            </div>
          )}
          
          {/* Hidden AI Extraction Component */}
          {uploadedFile && (
            <GeminiBillExtractor
              file={uploadedFile}
              onExtractionComplete={handleAiExtractionComplete}
            />
          )}
        </div>
      </DialogContent>
    </Dialog>
  );
}