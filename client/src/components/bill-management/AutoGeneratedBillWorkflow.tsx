import { useState } from 'react';
import { useForm } from 'react-hook-form';
import { zodResolver } from '@hookform/resolvers/zod';
import { z } from 'zod';
import { useMutation, useQueryClient } from '@tanstack/react-query';
import { apiRequest } from '@/lib/queryClient';
import { useLanguage } from '@/hooks/use-language';
import { useToast } from '@/hooks/use-toast';
import type { Bill } from '@shared/schema';

import {
  Dialog,
  DialogContent,
  DialogHeader,
  DialogTitle,
} from '@/components/ui/dialog';
import {
  Form,
  FormControl,
  FormField,
  FormItem,
  FormLabel,
  FormMessage,
} from '@/components/ui/form';
import { Input } from '@/components/ui/input';
import { Textarea } from '@/components/ui/textarea';
import { Button } from '@/components/ui/button';
import {
  Select,
  SelectContent,
  SelectItem,
  SelectTrigger,
  SelectValue,
} from '@/components/ui/select';
import { Card, CardContent, CardHeader, CardTitle } from '@/components/ui/card';
import { Badge } from '@/components/ui/badge';
import { Calendar, Sparkles } from 'lucide-react';

interface AutoGeneratedBillWorkflowProps {
  autoGeneratedBill: Bill | null;
  isOpen: boolean;
  onClose: () => void;
  onSuccess?: (billId: string) => void;
}

const newBillSchema = z.object({
  title: z.string().min(1, 'Title is required'),
  description: z.string().optional(),
  category: z.enum([
    'insurance',
    'maintenance',
    'salary',
    'utilities',
    'cleaning',
    'security',
    'landscaping',
    'professional_services',
    'administration',
    'repairs',
    'supplies',
    'taxes',
    'technology',
    'reserves',
    'other',
  ]),
  vendor: z.string().optional(),
  totalAmount: z.string().min(1, 'Amount is required'),
  startDate: z.string().min(1, 'Start date is required'),
  endDate: z.string().optional(),
  notes: z.string().optional(),
});

type NewBillFormData = z.infer<typeof newBillSchema>;

export default function AutoGeneratedBillWorkflow({
  autoGeneratedBill,
  isOpen,
  onClose,
  onSuccess,
}: AutoGeneratedBillWorkflowProps) {
  const { t } = useLanguage();
  const { toast } = useToast();
  const queryClient = useQueryClient();

  // Form setup with pre-populated data from auto-generated bill
  const form = useForm<NewBillFormData>({
    resolver: zodResolver(newBillSchema),
    defaultValues: {
      title: autoGeneratedBill?.title || '',
      description: autoGeneratedBill?.description || '',
      category: autoGeneratedBill?.category || 'other',
      vendor: autoGeneratedBill?.vendor || '',
      totalAmount: autoGeneratedBill?.totalAmount?.toString() || '',
      startDate: new Date().toISOString().split('T')[0], // Suggest current date
      endDate: autoGeneratedBill?.endDate || '',
      notes: autoGeneratedBill?.notes || '',
    }
  });

  // Create bill from auto-generated template mutation
  const createBillMutation = useMutation({
    mutationFn: async (data: NewBillFormData) => {
      const requestBody = {
        templateId: autoGeneratedBill?.id || '',
        suggestedDate: data.startDate,
        newBillData: {
          title: data.title,
          description: data.description,
          category: data.category,
          vendor: data.vendor,
          totalAmount: parseFloat(data.totalAmount),
          startDate: data.startDate,
          endDate: data.endDate,
          notes: data.notes,
          status: 'draft',
        },
      };

      // Note: File upload functionality can be added later if needed
      // For now, we'll focus on the core bill creation workflow

      return apiRequest('POST', '/api/bills/from-auto-generated', requestBody);
    },
    onSuccess: (response: any) => {
      queryClient.invalidateQueries({ queryKey: ['/api/bills'] });
      queryClient.invalidateQueries({ queryKey: ['/api/bills/auto-generated'] });
      
      toast({
        title: 'Success',
        description: 'Bill created successfully from auto-generated template',
      });
      
      onSuccess?.(response.bill?.id);
      onClose();
      form.reset();
    },
    onError: (error: any) => {
      toast({
        title: 'Error',
        description: error.message || 'Failed to create bill from template',
        variant: 'destructive',
      });
    }
  });


  const onSubmit = (data: NewBillFormData) => {
    createBillMutation.mutate(data);
  };

  if (!autoGeneratedBill) return null;

  return (
    <Dialog open={isOpen} onOpenChange={onClose}>
      <DialogContent className="max-w-2xl max-h-[90vh] overflow-y-auto">
        <DialogHeader>
          <DialogTitle className="flex items-center gap-2">
            <Sparkles className="h-5 w-5 text-blue-500" />
            Create Bill from Auto-Generated Template
          </DialogTitle>
        </DialogHeader>

        <div className="space-y-6">


          {/* Bill Form */}
          <Form {...form}>
            <form onSubmit={form.handleSubmit(onSubmit)} className="space-y-4">
              <div className="grid grid-cols-2 gap-4">
                {/* Title */}
                <FormField
                  control={form.control}
                  name="title"
                  render={({ field }) => (
                    <FormItem>
                      <FormLabel>Title *</FormLabel>
                      <FormControl>
                        <Input {...field} data-testid="input-bill-title" />
                      </FormControl>
                      <FormMessage />
                    </FormItem>
                  )}
                />

                {/* Category */}
                <FormField
                  control={form.control}
                  name="category"
                  render={({ field }) => (
                    <FormItem>
                      <FormLabel>Category *</FormLabel>
                      <Select onValueChange={field.onChange} value={field.value}>
                        <FormControl>
                          <SelectTrigger data-testid="select-category">
                            <SelectValue placeholder="Select category" />
                          </SelectTrigger>
                        </FormControl>
                        <SelectContent>
                          <SelectItem value="insurance">Insurance</SelectItem>
                          <SelectItem value="maintenance">Maintenance</SelectItem>
                          <SelectItem value="salary">Salary</SelectItem>
                          <SelectItem value="utilities">Utilities</SelectItem>
                          <SelectItem value="cleaning">Cleaning</SelectItem>
                          <SelectItem value="security">Security</SelectItem>
                          <SelectItem value="landscaping">Landscaping</SelectItem>
                          <SelectItem value="professional_services">Professional Services</SelectItem>
                          <SelectItem value="administration">Administration</SelectItem>
                          <SelectItem value="repairs">Repairs</SelectItem>
                          <SelectItem value="supplies">Supplies</SelectItem>
                          <SelectItem value="taxes">Taxes</SelectItem>
                          <SelectItem value="technology">Technology</SelectItem>
                          <SelectItem value="reserves">Reserves</SelectItem>
                          <SelectItem value="other">Other</SelectItem>
                        </SelectContent>
                      </Select>
                      <FormMessage />
                    </FormItem>
                  )}
                />

                {/* Vendor */}
                <FormField
                  control={form.control}
                  name="vendor"
                  render={({ field }) => (
                    <FormItem>
                      <FormLabel>Vendor</FormLabel>
                      <FormControl>
                        <Input {...field} data-testid="input-vendor" />
                      </FormControl>
                      <FormMessage />
                    </FormItem>
                  )}
                />

                {/* Total Amount */}
                <FormField
                  control={form.control}
                  name="totalAmount"
                  render={({ field }) => (
                    <FormItem>
                      <FormLabel>Total Amount *</FormLabel>
                      <FormControl>
                        <Input 
                          placeholder="0.00" 
                          type="number" 
                          step="0.01" 
                          {...field}
                          data-testid="input-total-amount"
                        />
                      </FormControl>
                      <FormMessage />
                    </FormItem>
                  )}
                />

                {/* Start Date */}
                <FormField
                  control={form.control}
                  name="startDate"
                  render={({ field }) => (
                    <FormItem>
                      <FormLabel className="flex items-center gap-2">
                        <Calendar className="h-4 w-4" />
                        Start Date * 
                        <Badge variant="outline" className="text-xs">
                          Suggested: Today
                        </Badge>
                      </FormLabel>
                      <FormControl>
                        <Input 
                          type="date" 
                          {...field}
                          data-testid="input-start-date"
                        />
                      </FormControl>
                      <FormMessage />
                    </FormItem>
                  )}
                />

                {/* End Date */}
                <FormField
                  control={form.control}
                  name="endDate"
                  render={({ field }) => (
                    <FormItem>
                      <FormLabel>End Date</FormLabel>
                      <FormControl>
                        <Input 
                          type="date" 
                          {...field}
                          data-testid="input-end-date"
                        />
                      </FormControl>
                      <FormMessage />
                    </FormItem>
                  )}
                />
              </div>

              {/* Description */}
              <FormField
                control={form.control}
                name="description"
                render={({ field }) => (
                  <FormItem>
                    <FormLabel>Description</FormLabel>
                    <FormControl>
                      <Textarea 
                        {...field} 
                        rows={3}
                        data-testid="textarea-description"
                      />
                    </FormControl>
                    <FormMessage />
                  </FormItem>
                )}
              />

              {/* Notes */}
              <FormField
                control={form.control}
                name="notes"
                render={({ field }) => (
                  <FormItem>
                    <FormLabel>Notes</FormLabel>
                    <FormControl>
                      <Textarea 
                        {...field} 
                        rows={2}
                        data-testid="textarea-notes"
                      />
                    </FormControl>
                    <FormMessage />
                  </FormItem>
                )}
              />

              {/* Action Buttons */}
              <div className="flex justify-between pt-4">
                <Button
                  type="button"
                  variant="outline"
                  onClick={onClose}
                  data-testid="button-cancel"
                >
                  Cancel
                </Button>
                <Button
                  type="submit"
                  disabled={createBillMutation.isPending}
                  data-testid="button-create-bill"
                >
                  {createBillMutation.isPending ? 'Creating...' : 'Create Bill'}
                </Button>
              </div>
            </form>
          </Form>

          {/* Info Note */}
          <div className="bg-blue-50 dark:bg-blue-950 border border-blue-200 dark:border-blue-800 rounded-lg p-3">
            <p className="text-sm text-blue-800 dark:text-blue-200">
              <strong>Note:</strong> Creating this bill will automatically update the source bill's end date 
              to prevent double counting in budget calculations.
            </p>
          </div>
        </div>
      </DialogContent>
    </Dialog>
  );
}