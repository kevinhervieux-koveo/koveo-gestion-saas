/**
 * @file Bills Page Shareable URL Tests
 * @description Verifies the bills page persists filters + sort to the URL
 *   and restores them on load so shared links keep working.
 *
 *   Note: the actual URL keys defined in client/src/pages/manager/bills.tsx
 *   are `sortField` + `sortDirection` (the original task brief listed
 *   `sortDir` informally; this test guards the real contract used by the
 *   parser/serializer).
 */

