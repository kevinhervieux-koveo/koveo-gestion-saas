    // Yearly aggregation helper that mirrors the yearly branch of
    // getChartData() in client/src/pages/manager/budget/index.tsx so we can
    // assert reconciliation invariants on aggregated yearly rows.
    // ---------------------------------------------------------------------
    function prepareYearlyChartData(
      forecast: any[],
      startIndex: number,
      length: number,
      configuredStartingBalance: number,
      forecastStartingBalance?: number,
    ): Array<{
      year: number;
      balanceStart: number;
      balanceEnd: number;
      revenue: number;
      spending: number;
      projects: number;
    }> {
      const sliced = forecast.slice(startIndex, startIndex + length);
      const yearlyData: Record<number, {
        revenue: number;
        spending: number;
        projects: number;
        balanceStart: number;
        balanceEnd: number;
        count: number;
      }> = {};

      sliced.forEach((item, index) => {
        const fy = item.year;
        if (!yearlyData[fy]) {
          yearlyData[fy] = { revenue: 0, spending: 0, projects: 0, balanceStart: 0, balanceEnd: 0, count: 0 };
        }
        const projectCosts = item.projectCosts || 0;
        yearlyData[fy].revenue += item.revenue;
        yearlyData[fy].spending += (item.spending - projectCosts);
        yearlyData[fy].projects += projectCosts;
        yearlyData[fy].count++;

        if (yearlyData[fy].count === 1) {
          const prevIndex = startIndex + index - 1;
          if (item.startingBalance !== undefined && item.startingBalance !== null) {
            yearlyData[fy].balanceStart = item.startingBalance;
          } else if (prevIndex >= 0 && forecast[prevIndex]) {
            yearlyData[fy].balanceStart = forecast[prevIndex].balance;
          } else if (forecastStartingBalance !== undefined && forecastStartingBalance !== null) {
            yearlyData[fy].balanceStart = forecastStartingBalance;
          } else {
            yearlyData[fy].balanceStart = configuredStartingBalance;
          }
        }
        yearlyData[fy].balanceEnd = item.balance;
      });

      return Object.entries(yearlyData)
        .sort(([a], [b]) => parseInt(a) - parseInt(b))
        .map(([year, d]) => ({
          year: parseInt(year),
          balanceStart: d.balanceStart,
          balanceEnd: d.balanceEnd,
          revenue: d.revenue,
          spending: d.spending,
          projects: d.projects,
        }));
    }

