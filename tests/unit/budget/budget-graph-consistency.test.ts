// Helper functions mirroring the actual implementations
function calculateMonthlyRecurringCosts(bills: BillData[]): number {
  return bills.reduce((total, bill) => {
    const costs = bill.costs.map(c => parseFloat(c) || 0);
    const billTotal = costs.reduce((sum, cost) => sum + cost, 0);
    
    switch (bill.schedulePayment) {
      case 'weekly':
        return total + (billTotal * 4.33);
      case 'monthly':
        return total + billTotal;
      case 'quarterly':
        return total + (billTotal / 3);
      case 'yearly':
        return total + (billTotal / 12);
      default:
        return total + billTotal;
    }
  }, 0);
}

function applyInflation(baseAmount: number, inflationRate: number, years: number): number {
  return baseAmount * Math.pow(1 + inflationRate, years);
}

function determineBalanceStatus(balance: number, minimumFund: number): 'red' | 'yellow' | 'green' {
  if (balance <= 0) return 'red';
  if (balance < minimumFund) return 'yellow';
  return 'green';
}

function getFinancialYear(year: number, month: number, financialYearStart: string): number {
  const startDate = new Date(financialYearStart);
  const fiscalStartMonth = startDate.getMonth() + 1;
  
  if (month < fiscalStartMonth) {
    return year;
  }
  return year;
}

function prepareChartData(
  forecastData: ForecastPeriod[],
  startingBalance: number,
  currentDate: Date = new Date()
): any[] {
  const currentYear = currentDate.getFullYear();
  const currentMonth = currentDate.getMonth() + 1;
  const monthNames = ['Jan', 'Feb', 'Mar', 'Apr', 'May', 'Jun', 'Jul', 'Aug', 'Sep', 'Oct', 'Nov', 'Dec'];
  
  return forecastData.map((item, index) => {
    const isFuture = item.year > currentYear || (item.year === currentYear && item.month >= currentMonth);
    
    // Prefer the per-period startingBalance returned by the backend so the
    // tooltip reconciles for every visible row, including the first one.
    let balanceStart: number;
    if (item.balanceStart !== undefined && item.balanceStart !== null) {
      balanceStart = item.balanceStart;
    } else if (index === 0) {
      balanceStart = startingBalance;
    } else {
      balanceStart = forecastData[index - 1].balance;
    }
    
    return {
      period: `${monthNames[item.month - 1]} ${item.year}`,
      month: item.month,
      year: item.year,
      revenue: item.revenue,
      spending: item.spending,
      balance: item.balance,
      balanceStart,
      balanceEnd: item.balance,
      netCashFlow: item.netCashFlow,
      capitalInvestments: item.capitalInvestment,
      isFuture,
    };
  });
}

function aggregateProjectsByMonth(
  projects: ProjectData[],
  year: number,
  month: number
): { total: number; urgent: number; suggested: number; notUrgent: number } {
  const monthlyProjects = projects.filter(p => {
    if (!p.includeInBudget) return false;
    
    if (p.plannedStartDate) {
      const projectDate = new Date(p.plannedStartDate);
      return projectDate.getFullYear() === year && projectDate.getMonth() + 1 === month;
    }
    
    return p.financialYear === year && month === 1;
  });
  
  return {
    total: monthlyProjects.reduce((sum, p) => sum + (p.totalBudget || p.estimatedCost || 0), 0),
    urgent: 0,
    suggested: 0,
    notUrgent: 0,
  };
}

function aggregateInvestmentsByMonth(
  investments: CapitalInvestment[],
  year: number,
  month: number,
  type: 'custom' | 'auto_generated' | 'all' = 'all'
): number {
  return investments
    .filter(inv => {
      const targetDate = new Date(inv.targetDate);
      const matchesMonth = targetDate.getFullYear() === year && targetDate.getMonth() + 1 === month;
      if (type === 'all') return matchesMonth;
      return matchesMonth && inv.type === type;
    })
    .reduce((sum, inv) => sum + inv.amount, 0);
}

function calculateTotalRevenue(
  baseRevenue: number,
  customRevenueLines: RevenueStream[],
  year: number,
  month: number
): number {
  let total = baseRevenue;
  
  customRevenueLines.forEach(line => {
    const startDate = new Date(line.startDate);
    const endDate = line.endDate ? new Date(line.endDate) : null;
    const currentDate = new Date(year, month - 1, 1);
    
    if (currentDate >= startDate && (!endDate || currentDate <= endDate)) {
      switch (line.frequency) {
        case 'monthly':
          total += line.amount;
          break;
        case 'quarterly':
          if ([1, 4, 7, 10].includes(month)) {
            total += line.amount;
          }
          break;
        case 'yearly':
          if (month === startDate.getMonth() + 1) {
            total += line.amount;
          }
          break;
      }
    }
  });
  
  return total;
}

