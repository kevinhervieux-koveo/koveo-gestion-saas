function renderWithProviders(ui: React.ReactElement) {
  const queryClient = new QueryClient({
    defaultOptions: {
      queries: { retry: false, refetchOnWindowFocus: false, gcTime: Infinity },
      mutations: { retry: false },
    },
  });
  return render(
    <QueryClientProvider client={queryClient}>
      <LanguageProvider>{ui}</LanguageProvider>
    </QueryClientProvider>,
  );
}

describe('ProjectForm edit error handling', () => {
  beforeEach(() => {
    jest.clearAllMocks();
    capturedOnErrorCallback = null;
  });

  it('shows an inline error alert and destructive toast when the mutation error callback fires', async () => {
    renderWithProviders(
      <ProjectForm
        isOpen={true}
        onOpenChange={() => {}}
        project={mockProject}
        mode="edit"
      />,
    );

    await screen.findByRole('dialog');

    expect(screen.queryByTestId('error-alert')).not.toBeInTheDocument();

    await act(async () => {
      capturedOnErrorCallback?.(new Error('Server error: could not save project'));
    });

    await waitFor(() => {
      const errorAlert = screen.queryByTestId('error-alert');
      expect(errorAlert).toBeInTheDocument();
    });

    expect(mockToast).toHaveBeenCalledWith(
      expect.objectContaining({ variant: 'destructive' }),
    );
  });

  it('does not show an error alert before any failure', async () => {
    renderWithProviders(
      <ProjectForm
        isOpen={true}
        onOpenChange={() => {}}
        project={mockProject}
        mode="edit"
      />,
    );

    await screen.findByRole('dialog');

    expect(screen.queryByTestId('error-alert')).not.toBeInTheDocument();
  });

  it('renders a submit button in edit mode', async () => {
    renderWithProviders(
      <ProjectForm
        isOpen={true}
        onOpenChange={() => {}}
        project={mockProject}
        mode="edit"
      />,
    );

    await screen.findByRole('dialog');

    const submitButton = screen.getByTestId('submit-button');
    expect(submitButton).toBeInTheDocument();
  });

  it('keeps the dialog open after a mutation failure', async () => {
    const onOpenChange = jest.fn();

    renderWithProviders(
      <ProjectForm
        isOpen={true}
        onOpenChange={onOpenChange}
        project={mockProject}
        mode="edit"
      />,
    );

    await screen.findByRole('dialog');

    await act(async () => {
      capturedOnErrorCallback?.(new Error('Network failure'));
    });

    await waitFor(() => {
      expect(screen.queryByTestId('error-alert')).toBeInTheDocument();
    });

    expect(onOpenChange).not.toHaveBeenCalledWith(false);
  });
});
