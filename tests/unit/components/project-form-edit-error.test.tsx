let capturedOnErrorCallback: ((error: Error) => void) | null = null;
let capturedMutate: jest.Mock;

jest.mock('../../../client/src/lib/common-hooks', () => {
  const actual = jest.requireActual('../../../client/src/lib/common-hooks') as Record<string, unknown>;
  return {
    ...actual,
    useCreateUpdateMutation: (opts: {
      onErrorCallback?: (error: Error) => void;
      errorTitle?: string | ((error: Error) => string);
      errorMessage?: string | ((error: Error) => string);
      silentError?: boolean;
      [key: string]: unknown;
    }) => {
      capturedOnErrorCallback = (error: Error) => {
        if (!opts.silentError) {
          const title = typeof opts.errorTitle === 'function' ? opts.errorTitle(error) : (opts.errorTitle ?? 'Error');
          const description = typeof opts.errorMessage === 'function' ? opts.errorMessage(error) : (opts.errorMessage ?? error.message ?? 'Failed to save item');
          mockToast({ title, description, variant: 'destructive' });
        }
        opts.onErrorCallback?.(error);
      };
      capturedMutate = jest.fn();
      return {
        mutate: capturedMutate,
        isPending: false,
        isError: false,
        isSuccess: false,
      };
    },
  };
});

