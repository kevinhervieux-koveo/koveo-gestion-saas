type ApiCall = { method: string; url: string; data?: unknown };
const apiCalls: ApiCall[] = [];

// Each test sets the AI response shape it wants returned by the mocked
// /api/bills/extract-data endpoint before triggering the fake upload.
let nextAiResponse: any = null;
let nextAiSuccess = true;

const mockApiRequest = jest.fn(
  async (method: string, url: string, data?: unknown) => {
    apiCalls.push({ method, url, data });

    if (url === '/api/bills/extract-data') {
      return {
        ok: true,
        status: 200,
        json: async () => ({
          success: nextAiSuccess,
          data: nextAiSuccess ? nextAiResponse : null,
          error: nextAiSuccess ? undefined : 'simulated extraction failure',
          metadata: { confidence: 0.9 },
        }),
      };
    }

    if (method === 'PUT' && url.startsWith('/api/bills/')) {
      return {
        ok: true,
        status: 200,
        json: async () => ({
          id: 'auto-gen-test',
          billNumber: 'BILL-001',
          buildingId: 'building-test',
        }),
      };
    }

    return { ok: true, status: 200, json: async () => ({}) };
  }
);

